# 4. ГРОМКОЕ ПРЕДУПРЕЖДЕНИЕ + ПОДТВЕРЖДЕНИЕ ПЕРЕД УДАЛЕНИЕМ ШИРОКИХ ПРАВИЛ
###############################################################################
