#!/usr/bin/env bash
###############################################################################
# setup-foreign-bastion.sh
#
# НАЗНАЧЕНИЕ:
#   Запускается на ЗАРУБЕЖНОМ VPS (выходной узел цепочки, тот же, что настраивает
#   setup-foreign-vps.sh). Запирает SSH этого VPS так, чтобы зайти по SSH можно
#   было ТОЛЬКО с публичного IP вашего РФ-сервера (он же бастион). Весь остальной
#   интернет к SSH-портам этого VPS доступа не получит.
#
#     [ПК/телефон] -> [РФ-сервер: бастион] --SSH--> [ЭТОТ зарубежный VPS]
#                                  ^
#                                  └── только с этого IP пускаем SSH
#
# ЗАЧЕМ ИМЕННО ЗДЕСЬ:
#   У зарубежного VPS обычно ЕСТЬ аварийная веб-консоль провайдера (KVM/serial/
#   VNC/recovery), поэтому в случае ошибки конфигурации фаервола вы не потеряете
#   управление окончательно — сможете откатить правила через консоль. На РФ-сервере
#   такой страховки может не быть, поэтому бастионный замок ставим на стороне VPS.
#
# КРИТИЧНО ПО БЕЗОПАСНОСТИ (порядок действий — строго такой, чтобы не залочиться):
#   (1) проверить, что RU_SERVER_IP задан и валиден (IPv4);
#   (2) определить ФАКТИЧЕСКИЕ SSH-порты (как в setup-foreign-vps.sh:
#       ss + ssh.socket ListenStream + Port из sshd_config/sshd_config.d + 22);
#   (3) СНАЧАЛА добавить ufw allow from RU_SERVER_IP к КАЖДОМУ SSH-порту;
#   (4) ГРОМКО предупредить и потребовать подтверждения (CONFIRM=yes или
#       интерактивный вопрос) ПЕРЕД удалением широких правил;
#   (5) ТОЛЬКО ПОТОМ удалить широкие правила (allow OpenSSH и широкие allow на
#       SSH-порты) — В ОБОИХ СЕМЕЙСТВАХ: и IPv4 ("Anywhere"), и IPv6
#       ("Anywhere (v6)"). Узкие правила «from RU_SERVER_IP» при этом остаются.
#   * Правило SOCKS-порта НЕ ТРОГАЕМ ни при каких условиях.
#   * ufw allow SSH делается ДО любого ufw enable — фаервол не отрезает SSH.
#
# IPv6 (finding #7): RU_SERVER_IP — это IPv4-хост, поэтому узкое разрешение
#   касается только IPv4. Но широкие ALLOW ufw создаёт ПАРАМИ (v4 + v6), а
#   удаление и пост-проверка раньше смотрели только на IPv4 — тогда широкий
#   SSH-allow по IPv6 ("Anywhere (v6)") мог остаться открыт всему миру, а скрипт
#   ложно печатал «замок активен». Теперь распознаём/удаляем широкие SSH-allow
#   ОБОИХ семейств, а «замок активен» печатаем ТОЛЬКО когда не осталось широкого
#   ни в v4, ни в v6 И default-политика входящих = deny (она в ufw общая для v4 и
#   v6 и закрывает не-разрешённый IPv6). Иначе печатаем ПРЕДУПРЕЖДЕНИЕ.
#
# ИДЕМПОТЕНТНОСТЬ:
#   Повторный запуск безопасен: узкие правила добавляются без дублей; широкие
#   правила, если их уже нет, просто пропускаются. ВАЖНО: широкий SSH-allow и
#   'ufw --force enable' выполняются ТОЛЬКО при ПЕРВОЙ настройке (когда ufw ещё
#   неактивен). На повторном запуске активного ufw мы НЕ трогаем SSH-правила и НЕ
#   делаем enable — иначе повторный прогон снова открыл бы SSH всему миру и откатил
#   бы уже суженный бастион.
#
# ВНИМАНИЕ (см. residual_risks):
#   * Если RU_SERVER_IP указан НЕВЕРНО (опечатка, динамический/изменившийся IP,
#     NAT/CGNAT провайдера РФ-сервера) — вы заблокируете себе SSH к VPS.
#     Спасёт ТОЛЬКО аварийная консоль провайдера (там: `ufw allow OpenSSH`).
###############################################################################

set -euo pipefail

# --- Подключаем общую библиотеку из каталога этого скрипта -------------------
# Общие функции (log/warn/err, валидаторы, detect_ssh_ports, ufw-гейт) живут в
# _lib.sh — единый источник правды. См. шапку _lib.sh про запуск как файл.
_SLV_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" 2>/dev/null && pwd)"
if [[ -z "${_SLV_DIR}" || ! -r "${_SLV_DIR}/_lib.sh" ]]; then
  echo "ОШИБКА: рядом со скриптом не найден _lib.sh. Скопируйте _lib.sh в тот же" >&2
  echo "        каталог и запускайте как файл (sudo bash $0), а не через stdin/pipe." >&2
  exit 1
fi
# shellcheck source=/dev/null
source "${_SLV_DIR}/_lib.sh"

###############################################################################
# КОНФИГУРАЦИЯ (правьте здесь или передавайте через переменные окружения)
###############################################################################

# ОБЯЗАТЕЛЬНО: публичный IPv4 вашего РФ-сервера (бастиона). Только с него будет
# разрешён SSH к этому VPS. Пример:
#   RU_SERVER_IP=203.0.113.5 bash setup-foreign-bastion.sh
RU_SERVER_IP="${RU_SERVER_IP:-}"

# Подтверждение удаления широких SSH-правил. Если != "yes" — скрипт спросит
# интерактивно (а при отсутствии терминала откажется удалять, чтобы не залочить).
CONFIRM="${CONFIRM:-}"

###############################################################################
# СЛУЖЕБНЫЕ ВАЛИДАТОРЫ И ДЕТЕКТ SSH-ПОРТОВ
###############################################################################
# valid_port / valid_ipv4_or_cidr / no_whitespace / detect_ssh_ports теперь
# берутся из _lib.sh (единый источник правды). Локальные копии удалены, чтобы не
# было дрейфа версий. Вызовы ниже резолвятся из либы.

###############################################################################
# СЛУЖЕБНЫЕ ПРОВЕРКИ
###############################################################################

if [[ "${EUID}" -ne 0 ]]; then
  echo "ОШИБКА: запустите скрипт от root (sudo bash $0)." >&2
  exit 1
fi

if ! command -v ufw >/dev/null 2>&1; then
  echo "ОШИБКА: ufw не установлен. Сначала прогоните setup-foreign-vps.sh," >&2
  echo "        который ставит и настраивает ufw, либо: apt-get install -y ufw" >&2
  exit 1
fi

###############################################################################
# 1. ПРОВЕРКА RU_SERVER_IP (ОБЯЗАТЕЛЕН И ВАЛИДЕН)
###############################################################################
echo "==> [1/6] Проверка RU_SERVER_IP..."

if [[ -z "${RU_SERVER_IP}" ]]; then
  echo "ОШИБКА: переменная RU_SERVER_IP не задана." >&2
  echo "        Укажите публичный IPv4 РФ-сервера, например:" >&2
  echo "          RU_SERVER_IP=203.0.113.5 bash $0" >&2
  exit 1
fi

# В адресе не должно быть пробелов (защита от подстановочных артефактов).
if ! no_whitespace "${RU_SERVER_IP}"; then
  echo "ОШИБКА: RU_SERVER_IP='${RU_SERVER_IP}' содержит пробельные символы." >&2
  exit 1
fi

# Валидация формата IPv4: 4 октета 0..255 (бастион — один конкретный хост, а не
# подсеть; так меньше шансов случайно открыть лишнее). CIDR здесь запрещаем явно.
if [[ "${RU_SERVER_IP}" == */* ]]; then
  echo "ОШИБКА: RU_SERVER_IP='${RU_SERVER_IP}' задан с CIDR/маской." >&2
  echo "        Ожидается ОДИН хост вида A.B.C.D (например 203.0.113.5)." >&2
  exit 1
fi

if ! valid_ipv4_or_cidr "${RU_SERVER_IP}"; then
  echo "ОШИБКА: RU_SERVER_IP='${RU_SERVER_IP}' не похож на корректный IPv4." >&2
  echo "        Ожидается вид A.B.C.D (например 203.0.113.5), без CIDR/маски." >&2
  exit 1
fi

# Запрет ведущих нулей (07, 001) — это не валидный/неоднозначный октет.
case "${RU_SERVER_IP}" in
  0[0-9]*|*.0[0-9]*)
    echo "ОШИБКА: RU_SERVER_IP='${RU_SERVER_IP}' содержит октет с ведущим нулём." >&2
    echo "        Уберите ведущие нули (например 203.0.113.5, не 203.00.113.05)." >&2
    exit 1
    ;;
esac

# Защита от очевидно-нерабочих адресов: с loopback/0.0.0.0/255.255.255.255
# забастионить нельзя (это гарантированный локаут).
case "${RU_SERVER_IP}" in
  127.*|0.0.0.0|255.255.255.255)
    echo "ОШИБКА: RU_SERVER_IP='${RU_SERVER_IP}' не может быть IP РФ-сервера" >&2
    echo "        (loopback/нулевой/широковещательный). Это привело бы к локауту." >&2
    exit 1
    ;;
esac

echo "    RU_SERVER_IP = ${RU_SERVER_IP} (валидный IPv4)."

###############################################################################
# 2. ОПРЕДЕЛЕНИЕ ФАКТИЧЕСКИХ SSH-ПОРТОВ (та же логика, что в setup-foreign-vps.sh)
###############################################################################
echo "==> [2/6] Определение текущих SSH-портов (учитываем socket-активацию)..."

# Единая каноническая функция-детектор (detect_ssh_ports из _lib.sh). Источники:
#   (a) реально слушающие сокеты sshd / ssh.socket (ss);
#   (b) ListenStream из юнитов ssh.socket / sshd.socket (источник истины при
#       socket-активации);
#   (c) директива Port из sshd_config и всех drop-in /etc/ssh/sshd_config.d/*.conf;
#   (d) страховка — классический 22 всегда в списке (вреда нет, локаут страшнее).
mapfile -t SSH_PORTS < <(detect_ssh_ports)

# Финальная страховочная валидация каждого порта (на случай неожиданного ввода
# из источников). Невалидный порт — это ошибка детекта, при ней сужать вслепую
# нельзя: выходим с ошибкой, а не глотаем через || true.
for p in "${SSH_PORTS[@]:-}"; do
  [[ -z "${p}" ]] && continue
  if ! valid_port "${p}"; then
    echo "ОШИБКА: детектор вернул нечисловой/недопустимый SSH-порт '${p}'." >&2
    exit 1
  fi
done

if [[ "${#SSH_PORTS[@]}" -eq 0 ]]; then
  # До сюда дойти не должно (22 добавляется всегда), но не рискуем.
  echo "ОШИБКА: не удалось определить ни одного SSH-порта — прекращаю," >&2
  echo "        чтобы не сузить фаервол вслепую." >&2
  exit 1
fi

echo "    SSH-порты, которые будут разрешены ТОЛЬКО с ${RU_SERVER_IP}: ${SSH_PORTS[*]}"

###############################################################################
# 2b. ОПРЕДЕЛЕНИЕ: ПЕРВАЯ НАСТРОЙКА ИЛИ ПОВТОРНЫЙ ЗАПУСК (анти-локаут / анти-откат)
###############################################################################
# Если ufw уже активен — значит бастион, скорее всего, уже настраивался ранее, и
# широкий SSH-allow + 'ufw --force enable' выполнять НЕЛЬЗЯ: иначе повторный прогон
# снова открыл бы SSH всему миру и перезаписал бы уже суженные правила (finding #4).
# В этом режиме мы лишь идемпотентно поддерживаем узкие правила.
#
# Робастный гейт — в либе (ufw_is_active): LC_ALL=C (вывод вне зависимости от
# локали), захват в переменную (НЕ пайп в grep → нет SIGPIPE под pipefail),
# fail-CLOSED (если статус не получить — считаем активным, чтобы НЕ переоткрыть
# SSH на уже забастионенном хосте). Код возврата: 0 = активен/неизвестно,
# 1 = точно inactive.
if ufw_is_active; then UFW_ACTIVE=1; else UFW_ACTIVE=0; fi

###############################################################################
# 3. ШАГ ЗАЩИТЫ: СНАЧАЛА ДОБАВЛЯЕМ УЗКИЕ ПРАВИЛА (allow from RU_SERVER_IP)
###############################################################################
echo "==> [3/6] Добавляю узкие SSH-правила (allow from ${RU_SERVER_IP}) ДО любого удаления..."

# Идемпотентность: проверяем по нормализованному списку правил, есть ли уже точно
# такое. ВАЖНО: на современном ufw действие печатается как "ALLOW IN" (с токеном
# IN), а на старом — как "ALLOW". Чтобы матчиться в обоих случаях, делаем токен
# IN опциональным и используем regex (-E), а не фиксированную строку (-F).
#
#   старый ufw:  2222/tcp  ALLOW     Anywhere
#   новый ufw:   2222/tcp  ALLOW IN  Anywhere
#   узкое:       2222/tcp  ALLOW IN  203.0.113.5
ufw_rule_matches() { # ufw_rule_matches <port> <ALLOW> <from-regex>
  local port="$1" action="$2" from="$3"
  # Экранируем точки в port/from, чтобы regex не трактовал их как «любой символ».
  local port_re="${port//./\\.}" from_re="${from//./\\.}"
  # IPv6-источник "Anywhere (v6)" содержит скобки — в ERE это группа, поэтому
  # ещё и экранируем '(' и ')' в from, чтобы они матчились как литералы.
  from_re="${from_re//(/\\(}"; from_re="${from_re//)/\\)}"
  # set -euo pipefail-безопасность: статус захватываем в переменную с LC_ALL=C
  # (НЕ 'ufw status | grep -q' как пайплайн-условие — закрытие grep'ом потока даёт
  # SIGPIPE(141) под pipefail; LC_ALL=C страхует от локализованного вывода ru_RU),
  # затем матчим уже из переменной — БЕЗ рано-закрывающегося пайпа.
  #
  # ПОЧЕМУ УБРАЛИ 'printf | tr | grep -qiE' (тот же класс, что чинили #2/#5/#7):
  #   под set -o pipefail 'grep -q' выходит на ПЕРВОМ совпадении и закрывает пайп
  #   → SIGPIPE(141) на 'tr'/'printf' → код пайплайна = 141 (а не 0), и
  #   [[ ]]/while/|| трактуют это как «нет совпадения» (false-success: широкое
  #   правило выживает, а скрипт рапортует успех). Срабатывает на >64KiB вывода
  #   ufw (тысячи правил). Канон файла — capture-then-match: матчим захваченную
  #   строку через [[ =~ ]] построчно, без пайпа.
  #
  # Матч построчный (как у grep): перебираем строки и тестируем КАЖДУЮ через
  # [[ =~ ]]. Регекс на каждой строке якорим с '^[[:space:]]*' (начало строки)
  # и '[[:space:]]*$' (конец строки) — это [[ =~ ]]-эквивалент grep-семантики.
  # '[[:space:]]+' сам поглощает прогоны пробелов столбцовой раскладки ufw,
  # поэтому 'tr -s' больше не нужен.
  #
  # ВАЖНО про '$': здесь это БАРЕ '$' (якорь конца строки), а НЕ '\$' как было в
  # grep-варианте. В старом коде '\$' нужен был лишь чтобы пережить разбор
  # двойных кавычек шелла перед передачей в grep. Теперь regex кладётся в
  # переменную и интерпретируется самим bash ([[ =~ ]]) — экранированный '\$' в
  # ERE-движках трактуется как ЛИТЕРАЛЬНЫЙ доллар (не якорь), что сломало бы
  # хвостовую привязку. Используем бэре '$'.
  #
  # IPv6: для v6-правил ufw добавляет суффикс "(v6)" В ОБА столбца — и в "To"
  # (порт: "22/tcp (v6)"), и в "From" (источник: "Anywhere (v6)"). Для v4-правил
  # суффикса нет ни там, ни там. Поэтому семейство адресов определяем по аргументу
  # <from>, а в столбце порта разрешаем НЕОБЯЗАТЕЛЬНЫЙ хвост " (v6)":
  #   * v4-источник "Anywhere": требуем, чтобы порт БЕЗ "(v6)" и строка
  #     заканчивалась на "Anywhere" — v6-строки оканчиваются на "(v6)" и не
  #     попадут сюда;
  #   * v6-источник "Anywhere (v6)": требуем "(v6)" в столбце порта И литерал
  #     "Anywhere (v6)" в источнике.
  # Итог:
  #   ufw_rule_matches "<p>/tcp" ALLOW "Anywhere"      -> ТОЛЬКО v4-строка;
  #   ufw_rule_matches "<p>/tcp" ALLOW "Anywhere (v6)" -> ТОЛЬКО v6-строка.
  local ufw_out line re
  # fail-CLOSED (HIGH #2): захват вывода 'ufw status' может НЕ удаться (ufw упал,
  # нет прав, сломан backend). Раньше код возврата гасился через '|| ufw_out=""' —
  # и пустая строка прогоняла while-цикл вхолостую → return 1 «правила нет». Тогда
  # delete_wide_ssh_rules ПРОПУСКАЛ удаление, any_wide_ssh_remains рапортовал чисто,
  # и скрипт печатал «замок активен», хотя широкий SSH мог быть ОТКРЫТ всему миру.
  # Это ломало fail-CLOSED-инвариант файла (ufw_is_active / default_incoming_is_deny
  # трактуют сбой как небезопасное состояние).
  #
  # ВАЖНО отличить «успех с пустым выводом» от «команда УПАЛА»: проверяем именно код
  # возврата (а не пустоту строки — у активного ufw без правил вывод тоже пустой по
  # содержимому, но команда успешна). При СБОЕ команды возвращаем 0 = «правило МОЖЕТ
  # присутствовать», чтобы удаление не пропускалось и не печатался ложный успех.
  # set -euo pipefail-безопасность: захват и проверку кода делаем РАЗДЕЛЬНО — присвоение
  # с подстановкой $() само по себе не роняет скрипт под set -e (берём $? сразу после).
  local _ufw_rc
  ufw_out="$(LC_ALL=C ufw status 2>/dev/null)"; _ufw_rc=$?
  if [[ "${_ufw_rc}" -ne 0 ]]; then
    # Команда не отработала — НЕ можем доказать отсутствие правила ⇒ fail-CLOSED.
    return 0
  fi
  # Действие сравниваем без учёта регистра: [[ =~ ]] чувствителен к регистру (в
  # отличие от 'grep -i'), а на разных версиях ufw действие бывает в разном
  # регистре. nocasematch включаем локально и снимаем сразу после цикла.
  if [[ "${from}" == "Anywhere" ]]; then
    # v4-ТОЛЬКО: порт без "(v6)", строка заканчивается на "Anywhere".
    re="^[[:space:]]*${port_re}[[:space:]]+${action}([[:space:]]+IN)?[[:space:]]+Anywhere[[:space:]]*$"
  elif [[ "${from}" == "Anywhere (v6)" ]]; then
    # v6-ТОЛЬКО: порт с суффиксом " (v6)", источник = "Anywhere (v6)".
    re="^[[:space:]]*${port_re}[[:space:]]+\(v6\)[[:space:]]+${action}([[:space:]]+IN)?[[:space:]]+Anywhere[[:space:]]+\(v6\)[[:space:]]*$"
  else
    # Конкретный источник (RU_SERVER_IP): узкое правило живёт только в IPv4-строке
    # (RU_SERVER_IP — IPv4), порт без "(v6)"; хвост — пробел или конец строки.
    re="^[[:space:]]*${port_re}[[:space:]]+${action}([[:space:]]+IN)?[[:space:]]+${from_re}([[:space:]]|$)"
  fi
  shopt -s nocasematch
  while IFS= read -r line; do
    if [[ "${line}" =~ ${re} ]]; then
      shopt -u nocasematch
      return 0
    fi
  done <<< "${ufw_out}"
  shopt -u nocasematch
  return 1
}

# КАНОН (анти-локаут / re-run без отката бастиона):
#   Широкий SSH-allow добавляем ТОЛЬКО при ПЕРВОЙ настройке (ufw ещё неактивен).
#   На повторном запуске активного ufw мы НЕ трогаем SSH-правила вообще — иначе
#   снова откроем 22 всему миру и перезапишем уже суженный бастион (finding #4).
if [[ "${UFW_ACTIVE}" -eq 0 ]]; then
  # ПЕРВЫЙ РАЗ: ufw ещё неактивен. Перед включением даём широкий SSH-allow на все
  # SSH-порты — это анти-локаут на момент enable, узкое правило добавляется тоже.
  #
  # set -euo pipefail-безопасность (MED #3): этот широкий allow раньше шёл ГОЛОЙ
  # командой 'ufw allow "${p}"/tcp'. Под set -euo pipefail транзиентный сбой ufw
  # (заблокированный backend, временный сбой nft) вернул бы ненулевой код и оборвал
  # бы ВЕСЬ скрипт прямо здесь — ДО добавления узких allow-from-правил и до 'ufw
  # --force enable', оставив бастион в полу-настроенном состоянии. Оборачиваем в
  # ufw_safe (как _lib.sh оборачивает СВОИ allow): ufw_safe = 'ufw "$@" || warn',
  # т.е. при сбое ufw он сам ГРОМКО предупреждает (warn) и возвращает 0, поэтому
  # скрипт не падает и продолжает к узким allow-from-правилам ниже. bastion НЕ
  # использует ufw_orchestrate (тут своя логика запирания) — поэтому правим именно
  # этот widen-цикл точечно.
  for p in "${SSH_PORTS[@]}"; do
    ufw_safe allow "${p}"/tcp
    echo "    (первичная настройка) Широкий allow на ${p}/tcp добавлен (анти-локаут на момент enable)."
  done
fi

# Узкие правила «allow from RU_SERVER_IP» добавляем ВСЕГДА (и в первый раз, и при
# повторном запуске) — это идемпотентно и безопасно.
for p in "${SSH_PORTS[@]}"; do
  # Целевое правило вида: "<port>/tcp  ALLOW [IN]  RU_SERVER_IP"
  if ufw_rule_matches "${p}/tcp" "ALLOW" "${RU_SERVER_IP}"; then
    echo "    Уже есть: allow from ${RU_SERVER_IP} to ${p}/tcp — пропускаю."
  else
    ufw allow from "${RU_SERVER_IP}" to any port "${p}" proto tcp
    echo "    Добавлено: allow from ${RU_SERVER_IP} to ${p}/tcp."
  fi
done

# Включаем ufw СЕЙЧАС ТОЛЬКО при первичной настройке (узкие И широкие SSH-правила
# уже на месте, поэтому доступ не теряется). Это до удаления широких правил.
# При повторном запуске (UFW_ACTIVE=1) enable НЕ делаем — фаервол уже активен, а
# повторный enable мог бы сбросить/перезаписать суженные правила бастиона.
if [[ "${UFW_ACTIVE}" -eq 0 ]]; then
  echo "    ufw был неактивен — включаю (узкие SSH-правила уже добавлены)."
  # set -euo pipefail-безопасность: 'ufw --force enable' голой командой под set -e
  # роняет ВЕСЬ скрипт, если enable вернул ненулевой код (типично на OpenVZ/в
  # контейнере без поддержки nf_tables) — причём ДО шага-4 (confirm) и шага-5
  # (удаление широких правил), оставляя бастион в полу-настроенном состоянии.
  # Sibling-скрипт mtproxy оборачивает enable в 'if ... then ... else ...' с
  # graceful degrade. Делаем так же: при сбое ГРОМКО предупреждаем и продолжаем
  # (узкие правила уже на месте), а не падаем молча.
  if ufw --force enable; then
    echo "    ufw включён."
  else
    echo "    ПРЕДУПРЕЖДЕНИЕ: 'ufw --force enable' завершился с ошибкой (часто на" >&2
    echo "    OpenVZ/в контейнере без nf_tables). Узкие SSH-правила (allow from" >&2
    echo "    ${RU_SERVER_IP}) уже добавлены, но фаервол, возможно, НЕ активен —" >&2
    echo "    проверьте вручную: ufw status. Скрипт продолжает без abort." >&2
  fi
else
  echo "    ufw уже активен — повторный запуск: SSH-правила не пересоздаю, enable не делаю."
fi

