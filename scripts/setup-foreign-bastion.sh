cat >&2 <<WARN

############################################################################
# !!! ВНИМАНИЕ: НЕОБРАТИМОЕ СУЖЕНИЕ ДОСТУПА К SSH !!!
#
# Сейчас будут УДАЛЕНЫ широкие правила, разрешающие SSH откуда угодно:
#     - профиль OpenSSH (allow OpenSSH)
#     - широкие allow на SSH-портах: ${SSH_PORTS[*]}
#
# После этого SSH к ЭТОМУ VPS останется ТОЛЬКО с IP: ${RU_SERVER_IP}
#
# Если ${RU_SERVER_IP} указан НЕВЕРНО (опечатка, не тот IP, динамический/CGNAT) —
# вы ЗАБЛОКИРУЕТЕ СЕБЕ SSH. Восстановление — ТОЛЬКО через аварийную консоль
# провайдера командой:   ufw allow OpenSSH
#
# ПЕРЕД ПОДТВЕРЖДЕНИЕМ убедитесь, что прямо СЕЙЧАС с РФ-сервера (${RU_SERVER_IP})
# проходит тестовое SSH-подключение к этому VPS в отдельной сессии.
############################################################################

WARN

# lock_complete отражает ФАКТИЧЕСКОЕ пост-состояние замка (а не только то, что
# подтверждение было дано): станет 1 лишь если после удаления реально не осталось
# широких ALLOW Anywhere. Итоговая сводка (шаг 6) опирается на него, а не на
# proceed_delete, чтобы не рапортовать ложный «замок активен».
lock_complete=0
proceed_delete="no"
if [[ "${CONFIRM}" == "yes" ]]; then
  echo "    CONFIRM=yes — подтверждение получено неинтерактивно."
  proceed_delete="yes"
elif [[ -t 0 ]]; then
  # Есть терминал — спрашиваем явно. Принимаем только точное 'yes'.
  read -r -p "Удалить широкие SSH-правила и оставить доступ только с ${RU_SERVER_IP}? Введите 'yes': " answer || answer=""
  if [[ "${answer}" == "yes" ]]; then
    proceed_delete="yes"
  else
    echo "    Подтверждение не получено (введено: '${answer:-<пусто>}')."
  fi
else
  # Нет терминала и нет CONFIRM=yes — НЕ удаляем (узкие правила уже добавлены,
  # это безопасное промежуточное состояние).
  echo "    Нет терминала и CONFIRM!=yes — широкие правила НЕ удаляю (безопасный отказ)." >&2
fi

###############################################################################
# 5. УДАЛЕНИЕ ШИРОКИХ ПРАВИЛ (только после подтверждения; SOCKS не трогаем)
###############################################################################
echo "==> [5/6] Удаление широких SSH-правил..."

# Удаление широких allow на КАЖДОМ SSH-порту вынесено в функцию: счётчики-предохра-
# нители (guard_tcp/guard_any) ДОЛЖНЫ объявляться через 'local' ВНУТРИ функции.
# Раньше они стояли через 'local' прямо в for-цикле на верхнем уровне скрипта —
# под set -e 'local' вне функции возвращает 1, и скрипт падал на первой итерации,
# оставляя бастион в частично-суженном состоянии (finding #1).
#
# КАНОН (set -euo pipefail-безопасность):
#   * НЕ используем возврат пайплайна 'ufw status | grep' как условие while —
#     это и SIGPIPE под pipefail, и зависимость от локали. Вместо этого матчим
#     по самостоятельной функции ufw_rule_matches (она уже захватывает вывод).
#   * НЕ маскируем ошибку 'ufw delete' через '|| break': молчаливый break прячет
#     реальный сбой удаления и оставил бы широкое правило на месте под видом
#     успеха (finding #3/#4). Если delete упал — это настоящая ошибка: печатаем
#     её и помечаем порт как незакрытый (через выход с ненулевым кодом), но не
#     роняем весь скрипт здесь (ловим вызов в '|| true' и перепроверяем
#     пост-состояние ниже).
#   * Off-by-one (finding #14): гард '(( guard < 4 ))' даёт максимум 4 итерации;
#     '(( guard <= 4 ))' давал бы 5.
delete_wide_ssh_rules() { # delete_wide_ssh_rules <port> [<port> ...]
  local p guard_tcp guard_any rc=0
  for p in "$@"; do
    # форма с /tcp
    # IPv6: гейт цикла матчит ОБА семейства — широкое v4 ("Anywhere") И широкое v6
    # ("Anywhere (v6)"). Одно 'ufw delete allow <spec>' снимает по одному правилу
    # за вызов (сначала, как правило, v4, затем парное v6), поэтому крутим цикл,
    # пока остаётся ХОТЬ ОДНО из двух — иначе v6-allow остался бы открыт всему миру.
    guard_tcp=0
    while ufw_rule_matches "${p}/tcp" "ALLOW" "Anywhere" \
       || ufw_rule_matches "${p}/tcp" "ALLOW" "Anywhere (v6)"; do
      if ! ufw delete allow "${p}/tcp"; then
        echo "    ОШИБКА: 'ufw delete allow ${p}/tcp' завершился с ошибкой." >&2
        rc=1; break
      fi
      echo "    Удалено широкое правило: allow ${p}/tcp (Anywhere v4/v6)."
      guard_tcp=$((guard_tcp + 1)); (( guard_tcp < 4 )) || break
    done
    # голая форма (any proto) — так же закрываем v4 и v6.
    guard_any=0
    while ufw_rule_matches "${p}" "ALLOW" "Anywhere" \
       || ufw_rule_matches "${p}" "ALLOW" "Anywhere (v6)"; do
      if ! ufw delete allow "${p}"; then
        echo "    ОШИБКА: 'ufw delete allow ${p}' завершился с ошибкой." >&2
        rc=1; break
      fi
      echo "    Удалено широкое правило: allow ${p} (Anywhere v4/v6)."
      guard_any=$((guard_any + 1)); (( guard_any < 4 )) || break
    done
  done
  return "${rc}"
}

# any_wide_ssh_remains <port> [<port> ...] -> 0 если ХОТЬ НА ОДНОМ порту ещё
# остаётся широкое ALLOW Anywhere (в любой из двух форм записи: /tcp или голый
# порт) и в ЛЮБОМ из двух семейств адресов (IPv4 "Anywhere" ИЛИ IPv6
# "Anywhere (v6)"). Используется для пост-проверки перед печатью финального
# сообщения: «открыт ТОЛЬКО с RU_SERVER_IP» печатаем лишь когда эта функция
# вернула 1 (широкого Anywhere не осталось НИ в v4, НИ в v6).
#
# IPv6: без явной проверки "Anywhere (v6)" мог бы остаться открыт всему миру, а
# скрипт ложно отрапортовал бы «замок активен» (finding #7).
any_wide_ssh_remains() {
  local p
  for p in "$@"; do
    if ufw_rule_matches "${p}/tcp" "ALLOW" "Anywhere" \
       || ufw_rule_matches "${p}/tcp" "ALLOW" "Anywhere (v6)" \
       || ufw_rule_matches "${p}" "ALLOW" "Anywhere" \
       || ufw_rule_matches "${p}" "ALLOW" "Anywhere (v6)"; then
      return 0
    fi
  done
  return 1
}

# default_incoming_is_deny -> 0 если политика по умолчанию для ВХОДЯЩИХ = deny
# (или reject). ufw применяет default-policy к ОБОИМ семействам (IPv4 и IPv6
# одновременно), поэтому 'deny incoming' закрывает в т.ч. весь не-разрешённый
# IPv6-трафик. Если же default = allow, то даже без широкого "Anywhere (v6)"
# ALLOW SSH-порт по IPv6 остаётся доступен всему миру — это НЕ замок.
#
# Источник истины — 'ufw status verbose' (строка вида "Default: deny (incoming),
# ..."). set -euo pipefail-безопасность: захват в переменную с LC_ALL=C, без пайпа
# в grep как условие. fail-CLOSED: если строку Default распознать не удалось —
# считаем, что НЕ deny (возвращаем 1), чтобы не рапортовать ложный успех.
default_incoming_is_deny() {
  local verbose
  verbose="$(LC_ALL=C ufw status verbose 2>/dev/null)" || verbose=""
  case "${verbose}" in
    *"Default: deny (incoming)"*|*"Default: reject (incoming)"*) return 0 ;;
    *) return 1 ;;
  esac
}

if [[ "${proceed_delete}" != "yes" ]]; then
  echo "    Пропущено: подтверждение не дано. Узкие правила (allow from ${RU_SERVER_IP})"
  echo "    уже активны. Чтобы завершить запирание позже, перезапустите с CONFIRM=yes:"
  echo "      RU_SERVER_IP=${RU_SERVER_IP} CONFIRM=yes bash $0"
else
  # Удаляем профиль OpenSSH (если есть). 'ufw delete' при отсутствии правила
  # вернёт ненулевой код — гасим его через || true.
  # Детектор учитывает опциональный токен IN (новый ufw: "ALLOW IN").
  #
  # set -euo pipefail-безопасность: статус захватываем в переменную с LC_ALL=C
  # (НЕ 'ufw status | grep' как условие — это и SIGPIPE под pipefail, и зависимость
  # от локали), затем матчим переменную БЕЗ рано-закрывающегося пайпа.
  #
  # ПОЧЕМУ УБРАЛИ 'printf | grep -qiE' (тот же класс, что чинили #2/#5/#7): под
  # set -o pipefail 'grep -q' выходит на первом совпадении и закрывает пайп →
  # SIGPIPE(141) на 'printf' → код пайплайна 141 ⇒ ложное «нет OpenSSH» (широкое
  # правило OpenSSH выжило бы, а скрипт рапортовал бы успех на >64KiB вывода ufw).
  # Канон файла — capture-then-match: перебираем захваченные строки через [[ =~ ]].
  ufw_status_openssh="$(LC_ALL=C ufw status 2>/dev/null)" || ufw_status_openssh=""
  openssh_present=0
  while IFS= read -r line; do
    # Матчим OpenSSH как отдельное слово на границах пробелов/краёв строки.
    if [[ "${line}" =~ (^|[[:space:]])OpenSSH([[:space:]]|$) ]]; then
      openssh_present=1
      break
    fi
  done <<< "${ufw_status_openssh}"
  if [[ "${openssh_present}" -eq 1 ]]; then
    ufw delete allow OpenSSH || true
    echo "    Удалено широкое правило: allow OpenSSH."
  else
    echo "    Широкого правила OpenSSH нет — пропускаю."
  fi

  # Удаляем широкие allow на КАЖДОМ SSH-порту в обеих формах записи:
  #   "<port>/tcp" (proto tcp) и голый "<port>" (any proto).
  # ufw печатает широкое правило источником "Anywhere" (v4) и "Anywhere (v6)".
  # Узкие правила «from RU_SERVER_IP» этим НЕ затрагиваются: у них другой
  # источник, и широкая спецификация удаления их не сматчит.
  #
  # ВАЖНО: 'ufw delete allow <spec>' удаляет ОДНО v4-правило за вызов; парное
  # v6-правило ("Anywhere (v6)") удаляется отдельным таким же вызовом — поэтому
  # повторяем delete, пока широкое правило (v4 ИЛИ v6) ещё присутствует. Это и
  # закрывает широкий SSH по IPv6, не оставляя его открытым.
  #
  # Ошибку delete функция НЕ маскирует молча: она печатает её и возвращает rc=1.
  # Здесь гасим ненулевой код через '|| true' (чтобы под set -e не упасть до
  # пост-проверки) и фиксируем флаг — реальный вывод об (не)успехе печатает
  # только пост-проверка фактического состояния ufw ниже.
  delete_failed=0
  delete_wide_ssh_rules "${SSH_PORTS[@]}" || delete_failed=1

  # ПОСТ-ПРОВЕРКА (КАНОН): «открыт ТОЛЬКО с RU_SERVER_IP» печатаем ТОЛЬКО если
  # после удаления реально НЕ осталось широкого ALLOW Anywhere ни на одном
  # SSH-порту (НИ в IPv4, НИ в IPv6) И при этом default-политика входящих = deny
  # (иначе SSH по IPv6 остаётся доступен всему миру даже без явного широкого
  # ALLOW). Иначе — ПРЕДУПРЕЖДЕНИЕ, а не ложный успех (finding #7).
  # set -euo pipefail-безопасность: НЕ используем 'fn && var=1' как самостоятельную
  # команду — при «ложном» исходе fn весь &&-список вернёт ненулевой код и уронит
  # скрипт под set -e. Поэтому ветвим явным if (его условие из-под set -e изъято).
  wide_remains=0; default_open=0
  if any_wide_ssh_remains "${SSH_PORTS[@]}"; then wide_remains=1; fi
  if ! default_incoming_is_deny; then default_open=1; fi
  if [[ "${wide_remains}" -eq 1 || "${default_open}" -eq 1 ]]; then
    if [[ "${wide_remains}" -eq 1 ]]; then
      echo "    ПРЕДУПРЕЖДЕНИЕ: на SSH-портах [${SSH_PORTS[*]}] ещё ОСТАЛОСЬ широкое" >&2
      echo "    правило ALLOW Anywhere (IPv4 и/или IPv6 'Anywhere (v6)') — SSH открыт" >&2
      echo "    НЕ только с ${RU_SERVER_IP}!" >&2
    fi
    if [[ "${default_open}" -eq 1 ]]; then
      echo "    ПРЕДУПРЕЖДЕНИЕ: default-политика входящих НЕ 'deny' — даже без явного" >&2
      echo "    широкого ALLOW SSH-порт по IPv6 может оставаться доступен всему миру." >&2
      echo "    Закройте по умолчанию входящие:   ufw default deny incoming" >&2
    fi
    if [[ "${delete_failed}" -eq 1 ]]; then
      echo "    Причина: команда 'ufw delete' выше завершилась с ошибкой (см. выше)." >&2
    fi
    echo "    Замок НЕ завершён. Разберите ufw status numbered вручную (проверьте и" >&2
    echo "    IPv6-строки 'Anywhere (v6)'), удалите оставшиеся широкие правила и" >&2
    echo "    при необходимости 'ufw default deny incoming', затем перезапустите" >&2
    echo "    с CONFIRM=yes." >&2
  else
    lock_complete=1
    echo "    Готово: SSH-порты [${SSH_PORTS[*]}] открыты ТОЛЬКО с ${RU_SERVER_IP}"
    echo "    (закрыто и по IPv4, и по IPv6: широких 'Anywhere'/'Anywhere (v6)' нет,"
    echo "     default incoming = deny)."
    echo "    Правило SOCKS-порта НЕ затронуто."
  fi
fi

