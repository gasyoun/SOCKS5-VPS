#!/usr/bin/env bash
#
# ============================================================================
#  setup-foreign-wireguard.sh
# ----------------------------------------------------------------------------
#  ГДЕ ЗАПУСКАТЬ: на ЗАРУБЕЖНОМ VPS (Ubuntu 24.04 / Debian 12-13), от root
#                (или через sudo).
#
#  ЧТО ДЕЛАЕТ: поднимает WireGuard-СЕРВЕР для ПОЛНОЦЕННОГО VPN-туннеля.
#  Телефон / ноутбук подключаются как WireGuard-клиент, и ВЕСЬ их IPv4-трафик
#  (AllowedIPs = 0.0.0.0/0) уходит в этот VPS и выходит в интернет
#  с его ИНОСТРАННОГО IP. Стек чисто IPv4 (IPv6 не туннелируется — см. п.9).
#
#  ВАЖНО — ЧЕМ ЭТО ОТЛИЧАЕТСЯ ОТ ТОЧЕЧНОГО ПРОКСИ (Proxifier/SOCKS5):
#    * Proxifier + SOCKS5 — это ТОЧЕЧНОЕ проксирование: вы вручную решаете,
#      какие именно приложения/правила гонять через прокси, остальное идёт
#      напрямую. Работает только там, где приложение/Proxifier умеет в прокси.
#    * Этот скрипт — это ПОЛНЫЙ VPN-ТУННЕЛЬ на уровне сетевого интерфейса.
#      Через него идёт ВЕСЬ трафик устройства, включая приложения без
#      поддержки прокси (мессенджеры, игры, мобильные приложения, системные
#      службы). Идеален для телефона, где Proxifier недоступен.
#    Это два РАЗНЫХ, независимых подхода к маршрутизации через зарубежный IP.
#
#  АРХИТЕКТУРА ЦЕПОЧКИ (этот скрипт = последнее, "выходное" звено):
#      [ПК/телефон] -> [РФ-сервер relay] -> [ЗАРУБЕЖНЫЙ VPS] -> интернет
#  Но WireGuard-клиент, созданный этим скриптом, может подключаться к VPS и
#  НАПРЯМУЮ (телефон -> VPS), если так удобнее. Маршрут через РФ-relay
#  настраивается отдельными скриптами и здесь не требуется.
#
#  БЕЗОПАСНОСТЬ:
#    * ufw ВСЕГДА разрешает OpenSSH и текущий SSH-порт ДО включения
#      файрвола — чтобы не заблокировать себе доступ.
#    * Никаких открытых портов без аутентификации: открыт только UDP-порт
#      WireGuard (трафик зашифрован и аутентифицирован ключами Curve25519
#      + pre-shared key) плюс SSH.
#    * Все приватные ключи и конфиги — chmod 600, создаются под umask 077.
#
#  ИДЕМПОТЕНТНОСТЬ: повторный запуск НЕ перетирает уже существующие ключи
#  сервера и клиента; конфиг и правила приводятся к нужному состоянию без
#  дублирования.
#
#  СИСТЕМНЫЙ ЮНИТ: скрипт НЕ создаёт собственный systemd-юнит — он использует
#  штатный шаблон wg-quick@.service из пакета wireguard-tools. Этот юнит уже
#  поставляется с корректными After=/Wants=network-online.target nss-lookup.target,
#  Type=oneshot и RemainAfterExit=yes, поэтому переопределять его не требуется
#  (изобретать свой ExecStart было бы хуже штатного). См. residual_risks про
#  network-online.target на минимальных образах.
# ============================================================================

set -euo pipefail

# --- Подключаем общую библиотеку из каталога этого скрипта ---
#  log/warn/err, валидаторы (valid_port, valid_ipv4_or_cidr, valid_ipv4,
#  no_whitespace, no_colon), detect_ext_iface, detect_ssh_ports, ufw_is_active,
#  ufw_safe и ufw_orchestrate берутся из _lib.sh — единый источник правды,
#  без локальных копий.
_SLV_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" 2>/dev/null && pwd)"
if [[ -z "${_SLV_DIR}" || ! -r "${_SLV_DIR}/_lib.sh" ]]; then
  echo "ОШИБКА: рядом со скриптом не найден _lib.sh. Скопируйте _lib.sh в тот же" >&2
  echo "        каталог и запускайте как файл (sudo bash $0), а не через stdin/pipe." >&2
  exit 1
fi
# shellcheck source=/dev/null
source "${_SLV_DIR}/_lib.sh"

# ----------------------------------------------------------------------------
#  ПЕРЕМЕННЫЕ КОНФИГУРАЦИИ (правьте при необходимости)
# ----------------------------------------------------------------------------

# UDP-порт, на котором слушает WireGuard-сервер (открывается в ufw).
WG_PORT="51820"

# Подсеть VPN. Внутренние адреса клиентов и сервера берутся отсюда.
WG_SUBNET="10.8.0.0/24"

# Внутренний (VPN) адрес самого сервера, без маски в Peer, с маской в Interface.
SERVER_WG_IP="10.8.0.1"

# Внутренний (VPN) адрес единственного клиента, создаваемого этим скриптом.
CLIENT_WG_IP="10.8.0.2"

# DNS, который будет прописан клиенту (чтобы DNS-запросы шли через туннель,
# а не утекали мимо). 1.1.1.1 = Cloudflare.
CLIENT_DNS="1.1.1.1"

# Имя WireGuard-интерфейса (он же имя конфига wg0.conf и systemd-юнита).
WG_IFACE="wg0"

# Каталог WireGuard и пути к ключам/конфигам.
WG_DIR="/etc/wireguard"
SERVER_PRIV="${WG_DIR}/server_private.key"   # приватный ключ сервера
SERVER_PUB="${WG_DIR}/server_public.key"     # публичный ключ сервера
CLIENT_PRIV="${WG_DIR}/client_private.key"   # приватный ключ клиента
CLIENT_PUB="${WG_DIR}/client_public.key"     # публичный ключ клиента
PSK_FILE="${WG_DIR}/client_preshared.key"    # pre-shared key (доп. слой защиты)
WG_CONF="${WG_DIR}/${WG_IFACE}.conf"         # конфиг сервера
CLIENT_CONF="${WG_DIR}/client-${WG_IFACE}.conf"  # готовый конфиг для телефона/ПК

# ----------------------------------------------------------------------------
#  СЛУЖЕБНЫЕ ФУНКЦИИ
# ----------------------------------------------------------------------------

# log/warn берутся из _lib.sh. die — специфичен для этого скрипта (фатальный
# выход): печатает сообщение через общий err и завершает работу с кодом 1.
die() { err "$*"; exit 1; }

# Проверка прав root.
require_root() {
  if [[ "${EUID}" -ne 0 ]]; then
    die "Запустите скрипт от root (или через sudo)."
  fi
}

# Проверка наличия бинаря в PATH.
need_cmd() {
  command -v "$1" >/dev/null 2>&1 || die "Не найден обязательный бинарь: $1"
}

# valid_port и valid_ipv4_or_cidr берутся из _lib.sh (единый источник правды;
# при провале вызывающий код делает exit 1 через die, НЕ '|| true').
# CIDR-подсеть, у которой маска ОБЯЗАТЕЛЬНА (a.b.c.d/<число>). WG_SUBNET структурно
# требует префикс: без него WG_PREFIX ("${WG_SUBNET##*/}") стал бы всей строкой IP
# вместо числа → битые Address-строки вида 10.8.0.1/10.8.0.0 в конфигах (находка #8).
valid_ipv4_cidr_required() {
  [[ "$1" == */* ]] || return 1            # маска обязательна
  valid_ipv4_or_cidr "$1"
}
# no_whitespace берётся из _lib.sh.

# ----------------------------------------------------------------------------
#  0. ПРЕДВАРИТЕЛЬНЫЕ ПРОВЕРКИ
# ----------------------------------------------------------------------------

require_root

# Жёсткая маска прав на ВСЁ, что создаём: новые файлы будут rw------- / каталоги 700.
# Это закрывает окно гонки: ключи рождаются уже с правами 600, ещё до chmod.
umask 077

# Санитизация значений конфигурации до их использования. При невалидном
# значении — немедленный exit 1 (через die), а НЕ молчаливое '|| true':
# битый порт/подсеть лучше отловить здесь, чем получить нерабочий ufw/wg.
valid_port "${WG_PORT}"            || die "Некорректный WG_PORT: ${WG_PORT} (ожидается 1..65535)."
# WG_SUBNET ДОЛЖНА быть CIDR с обязательной маской (a.b.c.d/<число>): из неё ниже
# извлекается WG_PREFIX для Address-строк. Maskless-значение здесь — фатальная ошибка.
valid_ipv4_cidr_required "${WG_SUBNET}" || die "Некорректная WG_SUBNET: ${WG_SUBNET} (ожидается IPv4/CIDR с обязательной маской, напр. 10.8.0.0/24)."
# SERVER_WG_IP и CLIENT_WG_IP — это ХОСТ-адреса (без маски): маска для Address-строк
# берётся отдельно из WG_PREFIX (${WG_SUBNET##*/}). Валидируем их СТРОГИМ valid_ipv4
# (из _lib.sh, CIDR запрещён): иначе значение с маской вида '10.8.0.1/24' прошло бы
# через valid_ipv4_or_cidr и дало битый 'Address = 10.8.0.1/24/24' в конфигах (#5).
valid_ipv4 "${SERVER_WG_IP}" || die "Некорректный SERVER_WG_IP: ${SERVER_WG_IP} (ожидается ХОСТ-адрес IPv4 без маски, напр. 10.8.0.1)."
valid_ipv4 "${CLIENT_WG_IP}" || die "Некорректный CLIENT_WG_IP: ${CLIENT_WG_IP} (ожидается ХОСТ-адрес IPv4 без маски, напр. 10.8.0.2)."
no_whitespace "${WG_IFACE}"        || die "Имя интерфейса WG_IFACE не должно содержать пробелов: '${WG_IFACE}'."

# ----------------------------------------------------------------------------
#  1. УСТАНОВКА ПАКЕТОВ
# ----------------------------------------------------------------------------
#  Имена пакетов проверены для Ubuntu 24.04 и Debian 12-13:
#    wireguard       — метапакет, тянет wireguard-tools (wg, wg-quick) и
#                      штатный шаблон systemd-юнита wg-quick@.service.
#    qrencode        — для вывода клиентского конфига в виде QR-кода.
#    iptables        — нужно для NAT/MASQUERADE в PostUp/PostDown.
#    ufw             — файрвол.
#    openssl         — для авто-генерации паролей (если потребуется в будущем)
#                      и как общая криптоутилита; обычно уже стоит.
log "Устанавливаю пакеты: wireguard, wireguard-tools, qrencode, iptables, ufw, openssl..."
export DEBIAN_FRONTEND=noninteractive
apt-get update -y
# wireguard-tools указываем явно — на минимальных образах метапакет wireguard
# может не подтянуть бинарь wg в нужном составе; явное имя надёжнее.
apt-get install -y wireguard wireguard-tools qrencode iptables ufw openssl

# Убеждаемся, что ключевые бинари реально появились, прежде чем их использовать.
need_cmd wg
need_cmd wg-quick
need_cmd ip
need_cmd iptables
need_cmd ufw
need_cmd qrencode

# ----------------------------------------------------------------------------
#  2. АВТООПРЕДЕЛЕНИЕ ВНЕШНЕГО СЕТЕВОГО ИНТЕРФЕЙСА
# ----------------------------------------------------------------------------
#  Берём интерфейс, через который идёт маршрут по умолчанию (выход в интернет).
#  На нём будем делать MASQUERADE. На cloud-VPS это ens3/eth0/enp1s0 и т.п. —
#  имя берётся из default route, поэтому конкретное имя не хардкодится.
#  Детект вынесен в _lib.sh: detect_ext_iface ищет поле ПОСЛЕ ключевого слова
#  'dev' (robust), а не жёсткий $5 — у маршрута без 'via'-шлюза поля сдвигаются
#  и старый $5 вернул бы мусор вроде 'dhcp' (прежний баг).
WAN_IFACE="$(detect_ext_iface || true)"
[[ -n "${WAN_IFACE}" ]] || die "Не удалось определить внешний интерфейс (нет default route)."
log "Внешний интерфейс (WAN): ${WAN_IFACE}"

# Внешний IP сервера — для подсказки в клиентском конфиге (Endpoint).
# Сначала пробуем адрес на WAN-интерфейсе; это поле потом можно поправить вручную.
# Используем `|| true`, чтобы пустой результат под set -o pipefail не уронил скрипт.
SERVER_PUBLIC_IP="$(ip -4 -o addr show dev "${WAN_IFACE}" 2>/dev/null \
  | awk '{print $4}' | cut -d/ -f1 | head -n1 || true)"
[[ -n "${SERVER_PUBLIC_IP}" ]] || SERVER_PUBLIC_IP="ВПИШИТЕ_ВНЕШНИЙ_IP_VPS"

# ----------------------------------------------------------------------------
#  3. ВКЛЮЧЕНИЕ IP-ФОРВАРДИНГА (персистентно)
# ----------------------------------------------------------------------------
#  Без ip_forward сервер не сможет маршрутизировать трафик клиента в интернет.
#
#  ВАЖНО — СТЕК ЧИСТО IPv4 (закрытие находки #5): туннель и весь NAT/FORWARD
#  построены только на IPv4 (iptables MASQUERADE, AllowedIPs=0.0.0.0/0). Поэтому
#  IPv6-форвардинг здесь НЕ включаем (net.ipv6.conf.all.forwarding оставляем
#  выключенным). Если бы мы его включили, то в связке с ufw
#  DEFAULT_FORWARD_POLICY=ACCEPT (она применяется и к ip6tables) на хосте с
#  публичным IPv6 этот бокс превратился бы в ОТКРЫТЫЙ IPv6-форвард-релей
#  (spam/DDoS-reflection): IPv6-пакеты форвардились бы без какой-либо фильтрации,
#  т.к. узкие FORWARD/MASQUERADE-правила в PostUp — только IPv4. v6 туннелю не
#  нужен и создаёт open-relay, поэтому не включаем его вовсе.
SYSCTL_FILE="/etc/sysctl.d/99-wireguard-forward.conf"
log "Включаю net.ipv4.ip_forward персистентно (v4-only стек): ${SYSCTL_FILE}"
cat > "${SYSCTL_FILE}" <<'EOF'
# WireGuard: разрешаем маршрутизацию (форвардинг) пакетов между интерфейсами.
# Стек чисто IPv4 — IPv6-форвардинг НАМЕРЕННО не включаем (см. находку #5:
# иначе при ufw DEFAULT_FORWARD_POLICY=ACCEPT получили бы открытый IPv6-релей).
net.ipv4.ip_forward = 1
EOF
chmod 644 "${SYSCTL_FILE}"   # sysctl-файл должен быть читаем для применения системой.
# Применяем немедленно, без перезагрузки. Ключ только один (IPv4), но всё равно
# не роняем скрипт на возможной ошибке применения — оставляя set -e в силе для остального.
sysctl -p "${SYSCTL_FILE}" >/dev/null 2>&1 || warn "Не удалось применить sysctl-ключ net.ipv4.ip_forward — проверьте вручную."

# Подстраховка от чужого/унаследованного включённого IPv6-форвардинга в рантайме:
# явно гасим его, чтобы DEFAULT_FORWARD_POLICY=ACCEPT не открыл v6-релей даже
# если форвардинг был включён ранее другим инструментом. Глушим ошибку, если в
# ядре нет IPv6 (ключа просто не существует) — это не критично для v4-only стека.
sysctl -w net.ipv6.conf.all.forwarding=0 >/dev/null 2>&1 || true

# ----------------------------------------------------------------------------
#  4. ГЕНЕРАЦИЯ КЛЮЧЕЙ (идемпотентно — существующие НЕ перетираем)
# ----------------------------------------------------------------------------
mkdir -p "${WG_DIR}"
chmod 700 "${WG_DIR}"

# Ключи сервера. Под umask 077 файлы рождаются уже с правами 600.
#
#  ЯКОРЬ ИДЕМПОТЕНТНОСТИ — ПУБЛИЧНЫЙ КЛЮЧ СЕРВЕРА (исправление находки HIGH #1).
#  Раньше гейт был по AND (-s SERVER_PRIV && -s SERVER_PUB). Но при оборванном
#  предыдущем запуске (half-run) публичный ключ мог остаться усечённым до 0 байт,
#  тогда как приватный уже валиден. AND-гейт уводил такую ситуацию в else и
#  МОЛЧА регенерировал ОБА серверных ключа → менялся публичный ключ сервера, и
#  ВСЕ уже доставленные клиенты ломались без какого-либо предупреждения.
#
#  Поэтому якорь идемпотентности — ПУБЛИЧНЫЙ ключ сервера (-s SERVER_PUB), по
#  образцу клиентского пути ниже:
#    * есть SERVER_PUB  -> публичный ключ сервера зафиксирован, пару НЕ трогаем
#      (даже если приватный отсутствует/битый — пересоздание публичного сломало
#      бы уже выданных клиентов; такую ситуацию чиним явным форсом, см. ниже);
#    * нет SERVER_PUB   -> сервер ещё не настраивался -> генерируем новую пару.
if [[ -s "${SERVER_PUB}" ]]; then
  if [[ -s "${SERVER_PRIV}" ]]; then
    log "Ключи сервера уже есть — повторно не генерирую."
  else
    # Публичный ключ сервера есть, приватного нет (или он битый): половинчатое
    # состояние от оборванного прогона. Регенерировать пару НЕЛЬЗЯ — это сменило
    # бы публичный ключ сервера и сломало бы всех уже выданных клиентов. Сервер
    # без приватного ключа не поднимется, но это явная, диагностируемая ошибка
    # ниже (пустой SERVER_PRIV_KEY), а не молчаливая инвалидация клиентов.
    warn "Публичный ключ сервера есть, приватный отсутствует или пуст (оборванный прогон?)."
    warn "Пару НЕ регенерирую: новый публичный ключ сервера сломал бы ВСЕХ уже выданных"
    warn "клиентов. Чтобы пересоздать серверный ключ ОСОЗНАННО (после чего ОБЯЗАТЕЛЬНО"
    warn "перевыпустить все клиентские конфиги), удалите ${SERVER_PRIV} и ${SERVER_PUB}"
    warn "вручную и перезапустите скрипт."
  fi
else
  log "Генерирую ключи сервера..."
  # Если рядом завалялся осиротевший приватный ключ без публичного — это остаток
  # оборванного прогона; раз публичного нет, ни один клиент на него не завязан,
  # поэтому пересоздаём пару целиком, предупредив об этом.
  [[ -e "${SERVER_PRIV}" ]] && warn "Серверный ключ пере-сгенерирован (публичного не было); старые клиенты, если были, надо перевыпустить."
  # Создаём приватный ключ отдельной командой (без пайпа), чтобы его права
  # были гарантированно 600 ещё до вычисления публичного, и чтобы pipefail
  # не маскировал сбой генерации.
  ( umask 077; wg genkey > "${SERVER_PRIV}" )
  wg pubkey < "${SERVER_PRIV}" > "${SERVER_PUB}"
fi

# Ключи клиента.
#
#  ЯКОРЬ ИДЕМПОТЕНТНОСТИ — ПУБЛИЧНЫЙ КЛЮЧ КЛИЕНТА (исправление регрессии #10).
#  Раньше гейт был по AND (-s CLIENT_PRIV && -s CLIENT_PUB). Но шаг #10 после
#  доставки конфига НАМЕРЕННО затирает приватный ключ клиента (shred/rm), а
#  публичный — нет (он не секрет). При AND-гейте отсутствие приватного уводило
#  ветку в else и ГЕНЕРИРОВАЛО НОВУЮ ПАРУ, переписывая [Peer] PublicKey в конфиге
#  сервера → любой повторный запуск молча инвалидировал уже выданного клиента.
#
#  Поэтому якорь идемпотентности — ТОЛЬКО публичный ключ (-s CLIENT_PUB):
#    * есть CLIENT_PUB  -> пир уже настроен, НИЧЕГО не регенерируем (даже если
#      приватный отсутствует — он вычищен после доставки, это норма);
#    * нет CLIENT_PUB   -> клиента ещё не было -> генерируем новую пару.
#  Для НОВОГО клиента нужен явный форс: удалите CLIENT_PUB (и при наличии
#  CLIENT_PRIV) вручную и перезапустите скрипт.
if [[ -s "${CLIENT_PUB}" ]]; then
  if [[ -s "${CLIENT_PRIV}" ]]; then
    log "Ключи клиента уже есть — повторно не генерирую."
  else
    # Публичный ключ есть, приватного нет: его вычистил шаг #10 после доставки.
    # Это НЕ повод регенерировать пару — пир на сервере уже настроен под этот
    # публичный ключ. Регенерация переписала бы [Peer] PublicKey и сломала бы
    # уже выданного клиента.
    log "Публичный ключ клиента есть, приватный отсутствует (вычищен после доставки, шаг #10) — пир уже настроен, пару НЕ регенерирую."
    warn "Приватный ключ клиента был безопасно удалён после выдачи конфига."
    warn "Переиспользуйте ранее выданный конфиг. Чтобы создать НОВОГО клиента —"
    warn "удалите ${CLIENT_PUB} (форс-сброс) и перезапустите скрипт."
  fi
else
  log "Генерирую ключи клиента..."
  ( umask 077; wg genkey > "${CLIENT_PRIV}" )
  wg pubkey < "${CLIENT_PRIV}" > "${CLIENT_PUB}"
fi

# Pre-shared key (дополнительный симметричный слой, в т.ч. PQ-резистентность).
if [[ -s "${PSK_FILE}" ]]; then
  log "Pre-shared key уже есть — повторно не генерирую."
else
  log "Генерирую pre-shared key..."
  ( umask 077; wg genpsk > "${PSK_FILE}" )
fi

# Жёсткие права на все секреты (паблик-ключи не секрет, но единообразие не вредит).
# CLIENT_PRIV может отсутствовать (вычищен шагом #10 после доставки) — chmod на
# несуществующий файл под set -e уронил бы скрипт, поэтому правим права только у
# реально существующих файлов.
for keyfile in "${SERVER_PRIV}" "${SERVER_PUB}" "${CLIENT_PRIV}" "${CLIENT_PUB}" "${PSK_FILE}"; do
  [[ -e "${keyfile}" ]] && chmod 600 "${keyfile}"
done

# Читаем значения ключей в переменные.
#  CLIENT_PRIV_KEY читаем ТОЛЬКО если приватный файл существует: после шага #10
#  он намеренно вычищен, и его отсутствие — штатная ситуация (пир уже настроен,
#  конфиг выдан). Пустая CLIENT_PRIV_KEY служит флагом «приватного нет» ниже.
SERVER_PRIV_KEY="$(cat "${SERVER_PRIV}")"
CLIENT_PUB_KEY="$(cat "${CLIENT_PUB}")"
SERVER_PUB_KEY="$(cat "${SERVER_PUB}")"
PSK_KEY="$(cat "${PSK_FILE}")"
if [[ -s "${CLIENT_PRIV}" ]]; then
  CLIENT_PRIV_KEY="$(cat "${CLIENT_PRIV}")"
else
  CLIENT_PRIV_KEY=""   # вычищен после доставки (#10) — клиентский конфиг не перегенерируем
fi

# Защита от пустых ключей (например, если предыдущий запуск оборвался).
#  CLIENT_PRIV_KEY в этот список НЕ входит: его законное отсутствие после #10 не
#  является ошибкой. Все остальные ключи обязаны быть непустыми.
for kv in "SERVER_PRIV_KEY:${SERVER_PRIV_KEY}" "CLIENT_PUB_KEY:${CLIENT_PUB_KEY}" \
          "SERVER_PUB_KEY:${SERVER_PUB_KEY}" "PSK_KEY:${PSK_KEY}"; do
  [[ -n "${kv#*:}" ]] || die "Пустой ключ ${kv%%:*}. Удалите файлы в ${WG_DIR} и перезапустите."
done

# Префикс маски подсети (например, 24 из 10.8.0.0/24) — для адреса Interface.
# Маска гарантированно присутствует: valid_ipv4_cidr_required отвергла бы maskless
# WG_SUBNET выше (находка #8), поэтому ${WG_SUBNET##*/} — это именно число, а не IP.
WG_PREFIX="${WG_SUBNET##*/}"

# ----------------------------------------------------------------------------
#  5. КОНФИГ СЕРВЕРА /etc/wireguard/wg0.conf
# ----------------------------------------------------------------------------
#  ВЫБОР ПОДХОДА К NAT: NAT/forward настраиваем ОДНИМ способом — через
#  PostUp/PostDown с iptables прямо в wg0.conf. Это атомарно привязано к
#  поднятию/опусканию интерфейса и НЕ дублируется с ufw-правилами:
#  ufw отвечает ТОЛЬКО за фильтрацию входящих (SSH + WG-порт) и за разрешение
#  форвардинга политикой (DEFAULT_FORWARD_POLICY=ACCEPT), а конкретные
#  FORWARD- и POSTROUTING/MASQUERADE-правила ставит iptables в PostUp.
#  Так нет двух источников правды и нет конфликтующих/дублирующихся правил.
#
#  ИДЕМПОТЕНТНОСТЬ ПРАВИЛ: PostUp сначала УДАЛЯЕТ возможный остаток правила
#  (|| true), затем добавляет — чтобы повторное up/up не плодило дубликаты,
#  даже если PostDown по какой-то причине не отработал.
#
#  ГЕЙТ ПО ИЗМЕНЕНИЮ КОНФИГА (находка #12): запоминаем SHA конфига ДО перезаписи,
#  чтобы ниже (шаг #7) перезапускать wg-quick ТОЛЬКО если конфиг реально изменился.
#  Безусловный restart на каждом re-run рвал бы живые WG-сессии без необходимости.
#  Если файла ещё нет — SHA пустой (отличается от любого нового → restart оправдан).
sha_of() { [[ -f "$1" ]] && sha256sum "$1" 2>/dev/null | awk '{print $1}' || true; }
WG_CONF_SHA_BEFORE="$(sha_of "${WG_CONF}")"

log "Пишу конфиг сервера: ${WG_CONF}"
cat > "${WG_CONF}" <<EOF
# ============================================================================
#  WireGuard-СЕРВЕР (${WG_IFACE}) — полный VPN-туннель, выход в интернет
#  с иностранного IP этого VPS.
#  Сгенерировано setup-foreign-wireguard.sh. Правьте осознанно.
# ============================================================================

[Interface]
# Внутренний адрес сервера в VPN-подсети.
Address    = ${SERVER_WG_IP}/${WG_PREFIX}
# UDP-порт прослушивания.
ListenPort = ${WG_PORT}
# Приватный ключ сервера.
PrivateKey = ${SERVER_PRIV_KEY}

# --- NAT/forward через iptables (единый источник правды) ---
# PostUp выполняется ПОСЛЕ поднятия интерфейса, PostDown — ПОСЛЕ опускания.
# Перед каждым -A делаем -D ... || true, чтобы не накапливать дубликаты.
# 1) Разрешаем форвардинг пакетов из VPN-подсети наружу и обратно.
# 2) MASQUERADE на внешнем интерфейсе — подменяем источник на IP VPS.
PostUp   = iptables -D FORWARD -i ${WG_IFACE} -j ACCEPT 2>/dev/null || true
PostUp   = iptables -A FORWARD -i ${WG_IFACE} -j ACCEPT
PostUp   = iptables -D FORWARD -o ${WG_IFACE} -j ACCEPT 2>/dev/null || true
PostUp   = iptables -A FORWARD -o ${WG_IFACE} -j ACCEPT
PostUp   = iptables -t nat -D POSTROUTING -s ${WG_SUBNET} -o ${WAN_IFACE} -j MASQUERADE 2>/dev/null || true
PostUp   = iptables -t nat -A POSTROUTING -s ${WG_SUBNET} -o ${WAN_IFACE} -j MASQUERADE
PostDown = iptables -D FORWARD -i ${WG_IFACE} -j ACCEPT 2>/dev/null || true
PostDown = iptables -D FORWARD -o ${WG_IFACE} -j ACCEPT 2>/dev/null || true
PostDown = iptables -t nat -D POSTROUTING -s ${WG_SUBNET} -o ${WAN_IFACE} -j MASQUERADE 2>/dev/null || true

[Peer]
# Единственный клиент (телефон/ноутбук).
PublicKey    = ${CLIENT_PUB_KEY}
# Pre-shared key — дополнительный симметричный слой защиты.
PresharedKey = ${PSK_KEY}
# Какие адреса принимаем ОТ этого клиента (его VPN-адрес как /32).
AllowedIPs   = ${CLIENT_WG_IP}/32
EOF

chmod 600 "${WG_CONF}"

# SHA конфига ПОСЛЕ перезаписи. Сравнение с WG_CONF_SHA_BEFORE даёт флаг
# CONFIG_CHANGED, по которому шаг #7 решает, нужен ли перезапуск службы (находка #12).
WG_CONF_SHA_AFTER="$(sha_of "${WG_CONF}")"
if [[ "${WG_CONF_SHA_BEFORE}" != "${WG_CONF_SHA_AFTER}" ]]; then
  CONFIG_CHANGED=1
else
  CONFIG_CHANGED=0
fi

# ----------------------------------------------------------------------------
#  6. ФАЙРВОЛ ufw — АНТИ-ЛОКАУТ ОРКЕСТРАЦИЯ ЧЕРЕЗ _lib.sh
# ----------------------------------------------------------------------------
#  КРИТИЧНО: SSH-правила и enable выполняет ЕДИНАЯ оркестрация ufw_orchestrate
#  из _lib.sh — без локальной копии анти-локаут-логики (рефакторинг #14):
#    * ПЕРВЫЙ запуск (ufw неактивен): ufw_orchestrate разрешает ВСЕ найденные
#      SSH-порты (detect_ssh_ports), и ТОЛЬКО если хотя бы один SSH-allow
#      применился — добавляет наши сервис-правила (add_service_rules) и делает
#      'ufw --force enable'. Если ни одно SSH-правило не прошло — возвращает 1,
#      и мы ОБЯЗАНЫ прекратить (default-deny без SSH = гарантированный локаут).
#    * ПОВТОРНЫЙ запуск (ufw уже активен): SSH-правила НЕ трогаем и enable НЕ
#      делаем (иначе перезатёрли бы возможное сужение бастиона, снова открыв
#      22/tcp всему миру — находка #4); добавляем лишь сервис-правила.
#  ufw_orchestrate ставит глобал UFW_FINAL_STATE (active|unknown), который мы
#  используем в финальном баннере ниже.
log "Настраиваю ufw (анти-локаут оркестрация: сначала SSH, чтобы не потерять доступ)..."

# add_service_rules — ИДЕМПОТЕНТНО добавляет ТОЛЬКО сервис-правила этого скрипта
# (UDP-порт WireGuard) через ufw_safe. SSH-порты и enable НЕ трогает — это делает
# ufw_orchestrate. ufw_safe (из _lib.sh) под set -e не валит скрипт на транзиентном
# сбое (xtables-lock и т.п.), а лишь предупреждает и продолжает (находка #6).
#
#  ОЧИСТКА ПЕРЕД ДОБАВЛЕНИЕМ (находка HIGH #2): сначала ufw_clear_port для текущего
#  WG_PORT/udp, затем allow. Зачем: ufw allow ИДЕМПОТЕНТНА лишь по точному совпадению
#  правила — при смене WG_PORT между запусками СТАРЫЙ порт остался бы открытым (его
#  никто не снимал), а ufw накапливал бы дубли разных форм одного и того же правила.
#  ufw_clear_port "${WG_PORT}" udp снимает все формы правила для текущего порта перед
#  чистым allow, устраняя дубликаты. (Прежний порт скрипт не запоминает между прогонами;
#  если бы хранил — следовало бы чистить и его. Минимальная гарантия здесь —
#  отсутствие дублей формы для текущего WG_PORT.)
add_service_rules() {
  ufw_clear_port "${WG_PORT}" udp
  ufw_safe allow "${WG_PORT}/udp"
}

# ВАЖНЫЙ ПОРЯДОК: DEFAULT_FORWARD_POLICY=ACCEPT правим ДО ufw_orchestrate, т.к.
# enable теперь выполняется ВНУТРИ оркестрации — а enable заново применяет
# DEFAULT_FORWARD_POLICY к цепочкам FORWARD. Если выставить ACCEPT после enable,
# на первом запуске политика не подхватилась бы без повторного reload.
# Разрешаем форвардинг трафика политикой ufw (по умолчанию он DROP) — иначе ufw
# зарубит проброс трафика клиента в интернет, несмотря на iptables-правила выше.
# ВНИМАНИЕ: это глобально разрешает форвардинг; узкую фильтрацию форварда
# обеспечивают iptables-правила в PostUp (только интерфейс wg0 и WG_SUBNET).
if grep -q '^DEFAULT_FORWARD_POLICY=' /etc/default/ufw; then
  sed -i 's/^DEFAULT_FORWARD_POLICY=.*/DEFAULT_FORWARD_POLICY="ACCEPT"/' /etc/default/ufw
else
  printf 'DEFAULT_FORWARD_POLICY="ACCEPT"\n' >> /etc/default/ufw
fi

# ПРИМЕНЕНИЕ FORWARD-POLICY НА RE-RUN (находка #6): /etc/default/ufw читается ufw
# только при enable/reload. На ПЕРВОМ запуске enable выполнится внутри
# ufw_orchestrate и сам подхватит свежую DEFAULT_FORWARD_POLICY=ACCEPT. Но на
# ПОВТОРНОМ запуске (ufw УЖЕ активен) ufw_orchestrate enable НЕ делает (анти-локаут:
# чтобы не перезатереть суженный бастион — находка #4), а 'reload' нигде не вызывался →
# изменённая forward-policy НЕ применилась бы к живым цепочкам FORWARD до ручного
# reload. Поэтому если ufw уже активен — делаем 'ufw reload' ЗДЕСЬ, ПОСЛЕ записи
# /etc/default/ufw, чтобы политика подхватилась в этом же прогоне. ufw_safe не валит
# скрипт под set -e на транзиентном сбое reload, а лишь предупреждает.
if ufw_is_active; then
  log "ufw уже активен — перечитываю /etc/default/ufw (ufw reload), чтобы применить DEFAULT_FORWARD_POLICY=ACCEPT к живым цепочкам."
  ufw_safe reload
fi

# Единый вызов анти-локаут-оркестрации. Сервис-правила (WG-порт) добавляются
# внутри неё; SSH-открытие и enable — тоже. Возврат 1 = первый запуск, но ни
# одно SSH-правило не применилось → НЕ продолжаем (иначе риск локаута).
# Код возврата фиксируем в __rc ДО любых других команд (под set -e и для ясности),
# затем явно прекращаем на ненулевом результате (находка #7).
ufw_orchestrate add_service_rules
__rc=$?
(( __rc == 0 )) || exit 1

# 6a. ЗАКРЫТИЕ IPv6-ФОРВАРДА (находки #5 и #3): DEFAULT_FORWARD_POLICY в ufw
#  применяется и к IPv4, и к IPv6 (ufw трогает ip6tables тоже). Поскольку стек
#  у нас чисто IPv4 и узких v6-FORWARD-правил мы НЕ ставим, оставлять v6-FORWARD
#  открытым нельзя — это был бы open-relay на хосте с публичным IPv6.
#
#  КРИТИЧНО — ПОРЯДОК (находка #3): этот DROP ОБЯЗАН идти ПОСЛЕ ufw_orchestrate.
#  enable (внутри оркестрации) заново применяет DEFAULT_FORWARD_POLICY=ACCEPT к
#  цепочке ip6tables FORWARD, перетирая любой ранее выставленный DROP. Если
#  выставить DROP ДО оркестрации, enable вернул бы политику в ACCEPT в том же
#  прогоне → итоговый v6-FORWARD оказался бы ACCEPT (открытый v6-форвард-релей).
#  Ставя -P FORWARD DROP здесь, ПОСЛЕ ufw_orchestrate, мы гарантируем, что по
#  завершении скрипта v6-FORWARD реально в DROP.
#
#  Fail-closed: явно выставляем политику ip6tables FORWARD = DROP. Если ядро
#  без IPv6 (ip6tables отсутствует/не работает) — глушим, это не критично.
if command -v ip6tables >/dev/null 2>&1; then
  ip6tables -P FORWARD DROP 2>/dev/null || warn "Не удалось выставить ip6tables FORWARD DROP (IPv6 в ядре отключён?) — для v4-only стека некритично."
fi

# ----------------------------------------------------------------------------
#  7. ЗАПУСК / ПЕРЕЗАПУСК СЛУЖБЫ WireGuard
# ----------------------------------------------------------------------------
#  Используем штатный шаблон wg-quick@.service (из wireguard-tools).
#  enable включает автозапуск; start/restart применяет конфиг.
#  Юнит уже имеет After/Wants=network-online.target — отдельный override не нужен.
#
#  ГЕЙТ ПЕРЕЗАПУСКА (находка #12): на уже поднятом интерфейсе перезапускаем
#  СТРОГО при изменении конфига (CONFIG_CHANGED=1). Безусловный restart на каждом
#  re-run рвал бы живые WG-сессии клиента без всякой нужды. Если конфиг не менялся —
#  служба уже работает с актуальным содержимым, оставляем её нетронутой.
log "Включаю и запускаю wg-quick@${WG_IFACE}..."
systemctl enable "wg-quick@${WG_IFACE}" >/dev/null 2>&1 || true
if systemctl is-active --quiet "wg-quick@${WG_IFACE}"; then
  if [[ "${CONFIG_CHANGED}" -eq 1 ]]; then
    log "Конфиг изменился — перезапускаю wg-quick@${WG_IFACE} для применения."
    systemctl restart "wg-quick@${WG_IFACE}"
  else
    log "Интерфейс уже поднят, конфиг не менялся — перезапуск НЕ требуется (живые сессии сохранены)."
  fi
else
  systemctl start "wg-quick@${WG_IFACE}"
fi

# Короткая проверка состояния.
if ! systemctl is-active --quiet "wg-quick@${WG_IFACE}"; then
  die "Служба wg-quick@${WG_IFACE} не активна. Смотрите: journalctl -u wg-quick@${WG_IFACE}"
fi

# ----------------------------------------------------------------------------
#  8. ГЕНЕРАЦИЯ КЛИЕНТСКОГО КОНФИГА
# ----------------------------------------------------------------------------
#  Это файл, который импортируется в приложение WireGuard на телефоне/ПК.
#  AllowedIPs = 0.0.0.0/0 => ПОЛНЫЙ туннель: ВЕСЬ IPv4-трафик идёт через VPS.
#  ::/0 НЕ добавляем: стек VPS чисто IPv4, иначе IPv6 клиента ушёл бы в
#  чёрную дыру (happy-eyeballs-таймауты). Подробности — в комментарии у AllowedIPs.
#
#  ГЕЙТ ПЕРЕГЕНЕРАЦИИ КОНФИГА (исправление регрессии #10): клиентский конфиг
#  содержит ПРИВАТНЫЙ ключ. Если приватного ключа нет (вычищен шагом #9 после
#  доставки), переписывать конфиг нечем — без CLIENT_PRIV_KEY вышла бы битая
#  запись `PrivateKey =` (пустая). В этом случае конфиг НЕ трогаем: клиент уже
#  выдан и настроен на сервере, переиспользуйте ранее полученный конфиг.
# Флаг: появился ли в этом запуске свежий клиентский конфиг с приватным ключом,
# который нужно выдать пользователю (печать/QR) и затем затереть в шаге #9.
# При отсутствии приватного ключа (вычищен после предыдущей доставки) остаётся 0,
# и шаг #9 НЕ пытается заново печатать/затирать уже выданные/удалённые секреты.
CLIENT_CONFIG_FRESH=0

if [[ -z "${CLIENT_PRIV_KEY}" ]]; then
  warn "Приватный ключ клиента отсутствует (вычищен после доставки, шаг #9) —"
  warn "клиентский конфиг НЕ перегенерирую (нечем заполнить PrivateKey)."
  warn "Переиспользуйте ранее выданный конфиг. Для НОВОГО клиента: удалите"
  warn "${CLIENT_PUB} и перезапустите скрипт (создаст новую пару и новый конфиг)."
else
  log "Генерирую клиентский конфиг: ${CLIENT_CONF}"
  cat > "${CLIENT_CONF}" <<EOF
# ============================================================================
#  WireGuard-КЛИЕНТ (телефон/ноутбук) — ПОЛНЫЙ VPN-туннель.
#  Импортируйте этот файл (или QR ниже) в приложение WireGuard.
#  ВЕСЬ трафик устройства пойдёт через зарубежный VPS и выйдет с его IP.
# ============================================================================

[Interface]
# Внутренний VPN-адрес клиента.
Address    = ${CLIENT_WG_IP}/${WG_PREFIX}
# Приватный ключ клиента.
PrivateKey = ${CLIENT_PRIV_KEY}
# DNS внутри туннеля (чтобы DNS-запросы не утекали мимо VPN).
DNS        = ${CLIENT_DNS}

[Peer]
# Публичный ключ сервера (этого VPS).
PublicKey    = ${SERVER_PUB_KEY}
# Pre-shared key (должен совпадать с серверным).
PresharedKey = ${PSK_KEY}
# Endpoint = внешний адрес VPS и UDP-порт. ПРОВЕРЬТЕ/ВПИШИТЕ внешний IP при
# необходимости (за NAT автоопределение может дать внутренний адрес).
Endpoint     = ${SERVER_PUBLIC_IP}:${WG_PORT}
# 0.0.0.0/0 => полный туннель для IPv4: ВЕСЬ IPv4-трафик идёт через VPS.
# ВНИМАНИЕ: стек этого сервера — чисто IPv4 (нет IPv6-адреса и IPv6-NAT),
# поэтому ::/0 здесь НЕ указываем. Иначе клиент маршрутизировал бы IPv6 в
# туннель, где он уходит в чёрную дыру (нет выходного IPv6 на VPS) — это даёт
# happy-eyeballs-таймауты на каждом IPv6-ресурсе. Если в будущем на VPS
# появится IPv6 (адрес на WAN + ip6tables MASQUERADE/NAT66), добавьте ', ::/0'.
AllowedIPs   = 0.0.0.0/0
# Keepalive — держит соединение живым через NAT (важно для мобильных сетей).
PersistentKeepalive = 25
EOF

  chmod 600 "${CLIENT_CONF}"
  CLIENT_CONFIG_FRESH=1
fi

# ----------------------------------------------------------------------------
#  9. ВЫВОД РЕЗУЛЬТАТА: текст конфига + QR-код
# ----------------------------------------------------------------------------
#  ГИГИЕНА СЕКРЕТОВ (закрытие находки #10):
#  Клиентский конфиг содержит ПРИВАТНЫЙ ключ клиента. Сервер не должен ни (а)
#  бессрочно хранить приватный ключ клиента на диске, ни (б) сливать его в
#  stdout/логи в неинтерактивном контексте (cloud-init, CI, ansible, перенаправ-
#  ление в файл) — там дамп ключа осел бы в логах оркестратора. Поэтому:
#   * полный текст конфига и QR печатаем ТОЛЬКО когда stdout — интерактивный
#     терминал ([[ -t 1 ]]); иначе печатаем лишь путь к файлу, без секрета;
#   * после показа НА ИНТЕРАКТИВНОМ терминале затираем клиентский конфиг и
#     приватный ключ клиента через `shred -u` — у пользователя уже есть копия
#     (импортирована/снят QR), а серверу хранить её незачем. Серверный приватный
#     ключ (нужен для работы туннеля) НЕ трогаем.
echo
log "================ ГОТОВО ================"
log "Сервер WireGuard поднят на ${WG_IFACE}, порт ${WG_PORT}/udp."
log "Внешний интерфейс для NAT: ${WAN_IFACE}"

# Итоговое состояние файрвола берём из UFW_FINAL_STATE (его выставила
# ufw_orchestrate): active => файрвол реально активен; unknown => enable мог не
# пройти (например, OpenVZ без nf_tables) — честно предупреждаем пользователя.
if [[ "${UFW_FINAL_STATE:-unknown}" == "active" ]]; then
  log "Файрвол ufw активен: разрешены SSH и ${WG_PORT}/udp (default-deny на остальном входящем)."
else
  warn "Файрвол ufw МОЖЕТ быть НЕ активен (enable не подтвердился — OpenVZ/без nf_tables?)."
  warn "Проверьте вручную: ufw status verbose — и при необходимости включите ufw сами."
fi

# Дальнейший вывод/выдача секретов имеют смысл ТОЛЬКО если в этом запуске был
# сгенерирован свежий клиентский конфиг. Если приватный ключ уже вычищен (#9),
# выдавать/печатать/затирать нечего — клиент уже настроен и выдан ранее.
if [[ "${CLIENT_CONFIG_FRESH}" -ne 1 ]]; then
  log "Клиент уже был выдан ранее; пир настроен на сервере."
  warn "Приватный ключ клиента вычищен после предыдущей доставки (#9) — конфиг не"
  warn "перегенерирован. Переиспользуйте ранее полученный конфиг. Для НОВОГО клиента:"
  warn "удалите ${CLIENT_PUB} и перезапустите скрипт."
  echo
  log "Проверка статуса на сервере:  wg show"
  exit 0
fi

log "Клиентский конфиг сохранён в: ${CLIENT_CONF}"
echo
warn "ПРОВЕРЬТЕ строку Endpoint в конфиге — там должен быть РЕАЛЬНЫЙ"
warn "внешний IP вашего VPS. Сейчас подставлено: ${SERVER_PUBLIC_IP}"

if [[ -t 1 ]]; then
  # --- ИНТЕРАКТИВНЫЙ ТЕРМИНАЛ: показываем секрет один раз и сразу затираем ---
  warn "Клиентский конфиг содержит ПРИВАТНЫЙ ключ клиента — печатается в терминал"
  warn "ниже ОДИН РАЗ. Передавайте его только по защищённому каналу; очистите историю/буфер."
  echo
  echo "----------------- НАЧАЛО КЛИЕНТСКОГО КОНФИГА -----------------"
  cat "${CLIENT_CONF}"
  echo "------------------ КОНЕЦ КЛИЕНТСКОГО КОНФИГА ------------------"
  echo
  log "QR-код для импорта в приложение WireGuard на телефоне:"
  echo
  qrencode -t ansiutf8 < "${CLIENT_CONF}"
  echo
  log "Импортируйте конфиг (файлом или сканом QR) ПРЯМО СЕЙЧАС и включите туннель."
  echo

  # Затираем клиентский секрет с диска: сервер не должен бессрочно его хранить.
  # shred -u перезаписывает и удаляет файл. Если shred недоступен — fallback на rm
  # с явным предупреждением, что файл удалён без безопасной перезаписи.
  if command -v shred >/dev/null 2>&1; then
    shred -u "${CLIENT_CONF}" "${CLIENT_PRIV}" 2>/dev/null \
      || warn "Не удалось shred-нуть клиентские секреты — удалите вручную: ${CLIENT_CONF} ${CLIENT_PRIV}"
  else
    rm -f "${CLIENT_CONF}" "${CLIENT_PRIV}"
    warn "Утилита shred недоступна — клиентские секреты удалены обычным rm (без безопасной перезаписи)."
  fi
  warn "Клиентский приватный ключ и конфиг УДАЛЕНЫ с сервера (безопасная гигиена)."
  warn "Если потеряете конфиг — перегенерируйте клиента: удалите ${CLIENT_PRIV}"
  warn "и ${CLIENT_PUB} в ${WG_DIR}, затем перезапустите этот скрипт (создаст"
  warn "новую пару ключей клиента и пропишет её в ${WG_CONF}/Peer)."
else
  # --- НЕИНТЕРАКТИВНЫЙ КОНТЕКСТ (cloud-init/CI/ansible/перенаправление в файл) ---
  # НЕ дампим приватный ключ клиента в stdout/логи. Печатаем только путь к файлу.
  # Файл оставляем на диске (chmod 600, выставлен выше) — забрать его должен
  # человек по защищённому каналу. После доставки рекомендуется shred вручную.
  warn "Неинтерактивный запуск (stdout — не терминал): приватный ключ клиента НЕ"
  warn "выводится в stdout/логи во избежание утечки секрета в логи оркестратора."
  log  "Клиентский конфиг (с приватным ключом) лежит здесь, права 600: ${CLIENT_CONF}"
  warn "Заберите его по защищённому каналу, а затем НЕ храните на сервере бессрочно:"
  warn "   shred -u ${CLIENT_CONF} ${CLIENT_PRIV}"
  warn "Перегенерация клиента: удалите ${CLIENT_PRIV} и ${CLIENT_PUB}, перезапустите скрипт."
fi

log "Проверка статуса на сервере:  wg show"
