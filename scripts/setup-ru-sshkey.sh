#!/usr/bin/env bash
#
# setup-ru-sshkey.sh
# ==================
# Запускается НА СЕРВЕРЕ В РФ.
#
# Назначение: подготовить беспарольный (key-based) SSH-доступ С РФ-СЕРВЕРА на
#   зарубежный VPS. Это фундамент для двух последующих сценариев:
#     (а) SSH-бастион / ProxyJump — РФ-сервер как прыжок на зарубежный VPS;
#     (б) autossh-туннель — постоянный обратный/прямой туннель к зарубежному VPS,
#         который должен подниматься headless (без ввода пароля при перезапуске).
#
#   Что делает скрипт:
#     1) создаёт ~/.ssh с правами 700;
#     2) если ключа нет — генерирует ed25519 БЕЗ passphrase (см. компромисс ниже),
#        приватный ключ 600, публичный 644;
#     3) определяет IP-ИСТОЧНИК, КОТОРЫЙ РЕАЛЬНО ВИДИТ ЗАРУБЕЖНЫЙ VPS (нужен для
#        from="..." ниже). На multi-homed / policy-routed РФ-сервере исходящий IP
#        для SSH (по таблице маршрутизации ядра) МОЖЕТ отличаться от того, что
#        отдаёт HTTP-проба (ipify/ifconfig.me ходят, возможно, через другой
#        интерфейс/прокси). Поэтому источник определяется во время самого
#        установочного ssh — на удалённой стороне читается $SSH_CONNECTION и
#        берётся его первое поле (это IP, с которого реально пришёл наш ключ).
#        HTTP-проба оставлена ТОЛЬКО как запасной вариант, если ssh-путь не дал IP;
#     4) ставит публичный ключ на зарубежный VPS С ОГРАНИЧЕНИЯМИ key-options
#        (from="<src-IP>",restrict,port-forwarding) — основной путь это РУЧНАЯ
#        запись options-префиксированной строки в authorized_keys (ssh-copy-id НЕ
#        умеет добавлять options и оставлен лишь как фолбэк БЕЗ ограничений).
#        Если при повторном запуске обнаружена ГОЛАЯ строка ключа (без options —
#        например, поставленная ранее через ssh-copy-id-фолбэк), она НА УДАЛЁННОЙ
#        стороне ПЕРЕЗАПИСЫВАЕТСЯ (awk index() по ДОСЛОВНОМУ телу ключа) на
#        options-префиксированную (апгрейд);
#        пароль зарубежного root запрашивается РОВНО ОДИН раз;
#     5) проверяет вход по ключу в BatchMode (без интерактива);
#     6) печатает источник-IP (как его видит зарубежный VPS) и итоговые ограничения ключа.
#
# КОМПРОМИСС passphrase vs автоматизация:
#   Ключ создаётся БЕЗ passphrase намеренно. autossh/бастион должны переподниматься
#   без участия человека (после ребута, обрыва связи, по systemd). Passphrase-ключ
#   потребовал бы ssh-agent с разлоченным ключом — а после ребута агент пуст, и
#   туннель не встанет сам. Платой за headless-автоматизацию является то, что
#   приватный ключ — это «носитель доступа как есть»: кто прочитает файл, тот войдёт.
#   Поэтому он лежит с правами 600 у владельца, а на зарубежной стороне доступ
#   стоит дополнительно ограничивать (firewall по IP, отдельный непривилегированный
#   пользователь, command="..."/from="..." в authorized_keys). Если headless НЕ нужен —
#   задайте KEY_PASSPHRASE ниже и используйте ssh-agent.
#
# Идемпотентность: скрипт можно запускать многократно. Существующий ключ НЕ
#   перезатирается; ssh-copy-id сам не плодит дубли в authorized_keys; ручной путь
#   дедуплицирует строку перед добавлением. БОЛЕЕ ТОГО — если ключ уже стоит, но
#   БЕЗ options (голая строка), повторный запуск НЕ просто пропускает установку, а
#   АПГРЕЙДИТ строку до ограниченной (from=/restrict/port-forwarding). Проверка
#   «вход по ключу уже работает» намеренно НЕ закорачивает этот апгрейд.
#
# Совместимость: Ubuntu 24.04 / Debian 12-13. Root НЕ требуется — работаем под тем
#   пользователем, от которого позже будет подниматься autossh/бастион (обычно root
#   на РФ-сервере). Где надо — ставим openssh-client через sudo, если он отсутствует.
#
set -euo pipefail

# --- Подключаем общую библиотеку из каталога этого скрипта ---
_SLV_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" 2>/dev/null && pwd)"
if [[ -z "${_SLV_DIR}" || ! -r "${_SLV_DIR}/_lib.sh" ]]; then
  echo "ОШИБКА: рядом со скриптом не найден _lib.sh. Скопируйте _lib.sh в тот же" >&2
  echo "        каталог и запускайте как файл (sudo bash $0), а не через stdin/pipe." >&2
  exit 1
fi
# shellcheck source=/dev/null
source "${_SLV_DIR}/_lib.sh"

# ============================================================================
#  КОНФИГУРАЦИЯ — отредактируйте значения ниже под себя
# ============================================================================

# --- Зарубежный VPS (Dante-сторона из setup-foreign-vps.sh) ---
FOREIGN_VPS_IP="203.0.113.10"     # ПУБЛИЧНЫЙ IP зарубежного VPS (ОБЯЗАТЕЛЬНО заменить!)
FOREIGN_SSH_PORT="22"             # SSH-порт зарубежного VPS
FOREIGN_SSH_USER="root"           # пользователь на зарубежном VPS, к которому даём доступ по ключу

# --- Локальный SSH-ключ на этом РФ-сервере ---
# Путь к ПРИВАТНОМУ ключу. Публичный — это тот же путь + ".pub".
# Тильда раскрывается ниже принудительно (в кавычках ~ сам по себе не раскроется).
KEY_PATH="~/.ssh/id_ed25519"

# Комментарий, вшиваемый в публичный ключ (видно в authorized_keys на той стороне —
# помогает потом понять, чей это ключ).
KEY_COMMENT="ru-relay->foreign-vps autossh/bastion"

# Passphrase для ключа. ПУСТО = headless-ключ без passphrase (нужно для autossh).
# Заполните, только если headless НЕ требуется и вы используете ssh-agent.
KEY_PASSPHRASE=""

# ============================================================================
#  СЛУЖЕБНЫЕ ФУНКЦИИ
# ============================================================================
# log()/warn()/err() и валидаторы (valid_port, valid_ipv4 и др.) приходят из
# общей библиотеки _lib.sh (подключена выше). Локальные копии здесь не нужны.

# Раскрываем ведущую тильду в $HOME (внутри кавычек bash этого не делает сам).
expand_tilde() {
    local p="$1"
    case "${p}" in
        "~")    printf '%s' "${HOME}" ;;
        "~/"*)  printf '%s' "${HOME}/${p#\~/}" ;;
        *)      printf '%s' "${p}" ;;
    esac
}

# Поставить пакет через sudo/apt, если бинарь отсутствует. Не падать молча.
ensure_pkg() {
    local bin="$1" pkg="$2"
    command -v "${bin}" >/dev/null 2>&1 && return 0
    warn "Не найден '${bin}' — пробую установить пакет '${pkg}'."
    if command -v apt-get >/dev/null 2>&1; then
        if [[ "${EUID}" -eq 0 ]]; then
            DEBIAN_FRONTEND=noninteractive apt-get update -y && \
            DEBIAN_FRONTEND=noninteractive apt-get install -y "${pkg}"
        elif command -v sudo >/dev/null 2>&1; then
            sudo DEBIAN_FRONTEND=noninteractive apt-get update -y && \
            sudo DEBIAN_FRONTEND=noninteractive apt-get install -y "${pkg}"
        else
            err "Нет ни root, ни sudo для установки '${pkg}'. Установите вручную и повторите."
            exit 1
        fi
    else
        err "apt-get недоступен. Установите '${bin}' вручную (пакет '${pkg}') и повторите."
        exit 1
    fi
    command -v "${bin}" >/dev/null 2>&1 || { err "Не удалось установить '${bin}'."; exit 1; }
}

# ============================================================================
#  0. ПРЕДВАРИТЕЛЬНЫЕ ПРОВЕРКИ
# ============================================================================

# Защита от запуска с незаполненными/пустыми плейсхолдерами.
if [[ -z "${FOREIGN_VPS_IP}" || "${FOREIGN_VPS_IP}" == "203.0.113.10" ]]; then
    err "Заполните FOREIGN_VPS_IP в начале скрипта реальным IP зарубежного VPS."
    exit 1
fi
if [[ -z "${FOREIGN_SSH_USER}" ]]; then
    err "FOREIGN_SSH_USER не должен быть пустым (например: root)."
    exit 1
fi
if ! valid_port "${FOREIGN_SSH_PORT}"; then
    err "FOREIGN_SSH_PORT должен быть числом 1..65535 (сейчас: '${FOREIGN_SSH_PORT}')."
    exit 1
fi
# Пустой KEY_PATH привёл бы к chmod 700 на текущий каталог (.) и ключу с именем
# '.pub' — это явная ошибка конфигурации, останавливаемся заранее.
if [[ -z "${KEY_PATH}" ]]; then
    err "KEY_PATH не должен быть пустым (например: ~/.ssh/id_ed25519)."
    exit 1
fi

# Зависимости: ssh/ssh-keygen (клиент). curl — для определения egress-IP в конце.
ensure_pkg ssh openssh-client
ensure_pkg ssh-keygen openssh-client
command -v curl >/dev/null 2>&1 || warn "curl не найден — egress-IP в конце определю фолбэком (или пропущу)."

# Раскрываем пути ключей один раз.
PRIV_KEY="$(expand_tilde "${KEY_PATH}")"
PUB_KEY="${PRIV_KEY}.pub"
SSH_DIR="$(dirname "${PRIV_KEY}")"

# Подстраховка: раскрытый путь не должен оказаться пустым/каталогом '.'.
if [[ -z "${PRIV_KEY}" || "${SSH_DIR}" == "." ]]; then
    err "KEY_PATH после раскрытия выглядит некорректно ('${PRIV_KEY}'). Укажите абсолютный путь или путь от ~."
    exit 1
fi

# Общие опции ssh для НЕинтерактивных проверок: не висеть на подтверждении хоста,
# писать ключ хоста в known_hosts, быстрый таймаут на коннект.
# ПРИМЕЧАНИЕ (TOFU/MITM): accept-new принимает ключ хоста при ПЕРВОМ коннекте без
# сверки. В недоверённой сети это окно для MITM на самом первом подключении.
# При повышенных требованиях замените на StrictHostKeyChecking=yes и заранее
# положите доверенный ключ хоста в ~/.ssh/known_hosts (ssh-keyscan со сверкой
# fingerprint вне канала).
SSH_COMMON_OPTS=(
    -p "${FOREIGN_SSH_PORT}"
    -o ConnectTimeout=10
    -o StrictHostKeyChecking=accept-new
)

# ============================================================================
#  1. КАТАЛОГ ~/.ssh С ПРАВАМИ 700
# ============================================================================

log "Готовлю каталог ${SSH_DIR} (права 700)..."
mkdir -p "${SSH_DIR}"
chmod 700 "${SSH_DIR}"

# ============================================================================
#  2. ГЕНЕРАЦИЯ КЛЮЧА ed25519 (идемпотентно: существующий НЕ перезатираем)
# ============================================================================

if [[ -f "${PRIV_KEY}" ]]; then
    log "Приватный ключ уже существует: ${PRIV_KEY} — НЕ перегенерирую (идемпотентность)."
    # Подстрахуемся по правам, даже если ключ создан ранее вручную.
    chmod 600 "${PRIV_KEY}"
    # Если публичной половины почему-то нет — восстановим из приватной.
    # ВНИМАНИЕ: если приватный ключ ЗАЩИЩЁН passphrase, ssh-keygen -y запросит её
    # интерактивно. Это допустимо (разовая операция восстановления).
    if [[ ! -f "${PUB_KEY}" ]]; then
        warn "Публичный ключ ${PUB_KEY} отсутствует — восстанавливаю из приватного."
        if ! ssh-keygen -y -f "${PRIV_KEY}" > "${PUB_KEY}"; then
            rm -f "${PUB_KEY}"
            err "Не удалось восстановить публичный ключ из ${PRIV_KEY} (возможно, нужна passphrase)."
            exit 1
        fi
        chmod 644 "${PUB_KEY}"
    fi
else
    log "Генерирую новый ed25519-ключ: ${PRIV_KEY}"
    if [[ -n "${KEY_PASSPHRASE}" ]]; then
        warn "Ключ создаётся С passphrase — для headless autossh потребуется ssh-agent."
    else
        log "Ключ создаётся БЕЗ passphrase (headless для autossh/бастиона — см. комментарий в шапке)."
    fi
    # -N "" => без passphrase; -C => комментарий; -f => путь. Без -q, чтобы видеть fingerprint.
    ssh-keygen -t ed25519 -a 100 -f "${PRIV_KEY}" -N "${KEY_PASSPHRASE}" -C "${KEY_COMMENT}"
    chmod 600 "${PRIV_KEY}"
    chmod 644 "${PUB_KEY}"
    log "Ключ создан. Приватный — 600, публичный — 644."
fi

# ============================================================================
#  3. ИСТОЧНИК-IP РФ-СЕРВЕРА (для from="..." в ключе)
# ============================================================================
# В from="..." должен попасть тот IP, КОТОРЫЙ РЕАЛЬНО УВИДИТ sshd зарубежного VPS,
# когда ключ придёт по таблице маршрутизации ядра. На multi-homed/policy-routed
# РФ-сервере этот IP может НЕ совпадать с тем, что отдаёт HTTP-проба (ipify/
# ifconfig.me могут уходить через другой интерфейс/прокси), и тогда foreign sshd
# отверг бы ключ с верным from= — туннель не встал бы.
#
# Поэтому НАСТОЯЩИЙ источник определяется во время установочного ssh: на удалённой
# стороне читается $SSH_CONNECTION и берётся его ПЕРВОЕ поле (client IP). Делается
# это на шаге 4 в рамках того же соединения, что и установка ключа. HTTP-проба
# ниже — лишь ЗАПАСНОЙ вариант (если ssh-путь не дал IP, например при чистом
# фолбэке через ssh-copy-id).

# Запасное определение исходящего IP через HTTP — НЕ основной источник (см. выше).
get_egress_ip_http() {
    local ip=""
    if command -v curl >/dev/null 2>&1; then
        ip="$(curl -fsS --max-time 10 https://api.ipify.org 2>/dev/null || true)"
        [[ -z "${ip}" ]] && ip="$(curl -fsS --max-time 10 https://ifconfig.me 2>/dev/null || true)"
    fi
    printf '%s' "${ip}"
}

# Валидация одиночного IPv4-хоста (CIDR запрещён) — чтобы не вписать в from="..."
# мусор (HTML об ошибке и т.п.) — берётся из _lib.sh как valid_ipv4.

# Собрать KEY_OPTIONS из источника-IP. Вызывается ПОСЛЕ того, как мы попытались
# узнать реальный источник (через ssh) и/или фолбэком (через HTTP).
#   restrict        => выключает shell/pty/agent-forwarding/X11/port-forwarding разом
#                       (безопасный дефолт; форвардинг возвращаем явно ниже);
#   port-forwarding => возвращает ТОЛЬКО форвардинг портов — он нужен для ssh -D/-L
#                       (SOCKS-туннель) и для autossh;
#   from="..."      => привязывает ключ к источнику-IP РФ-релея (ключ бесполезен с
#                       чужого IP).
# Результат кладёт в глобальные KEY_OPTIONS и нормализует SERVER_EGRESS_IP.
build_key_options() {
    local src="$1"
    if valid_ipv4 "${src}"; then
        SERVER_EGRESS_IP="${src}"
        KEY_OPTIONS="from=\"${src}\",restrict,port-forwarding"
    else
        KEY_OPTIONS="restrict,port-forwarding"
        SERVER_EGRESS_IP="неизвестен (определите вручную: curl https://api.ipify.org)"
    fi
}

# Источник-IP (как его видит зарубежный VPS). Заполнится на шаге 4 из $SSH_CONNECTION;
# пока пусто — и KEY_OPTIONS соберём, когда будет ясно, что удалось узнать.
SERVER_EGRESS_IP=""
KEY_OPTIONS=""

# ============================================================================
#  4. КОПИРОВАНИЕ / АПГРЕЙД ПУБЛИЧНОГО КЛЮЧА НА ЗАРУБЕЖНОМ VPS
# ============================================================================

key_login_works() {
    # BatchMode=yes        => никаких интерактивных запросов пароля; либо ключ, либо отказ.
    # IdentitiesOnly=yes   => предлагать ТОЛЬКО наш -i ключ, игнорируя ssh-agent и
    #                          ключи по умолчанию. Без этого посторонний ключ из агента
    #                          мог бы ложно «подтвердить» вход и заставить скрипт
    #                          пропустить копирование ИМЕННО нашего ключа.
    ssh "${SSH_COMMON_OPTS[@]}" \
        -o BatchMode=yes \
        -o IdentitiesOnly=yes \
        -i "${PRIV_KEY}" \
        "${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}" 'echo OK' 2>/dev/null | grep -qx 'OK'
}

# Источник-IP, как его видит зарубежный sshd: первое поле $SSH_CONNECTION в УЖЕ
# работающей по ключу сессии (без пароля, BatchMode). Возвращает пусто, если
# ключ ещё не пускает или $SSH_CONNECTION недоступен.
src_ip_via_key() {
    ssh "${SSH_COMMON_OPTS[@]}" \
        -o BatchMode=yes \
        -o IdentitiesOnly=yes \
        -i "${PRIV_KEY}" \
        "${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}" \
        'set -- $SSH_CONNECTION; printf %s "$1"' 2>/dev/null
}

# Тело публичного ключа БЕЗ options (одна строка). Им дедуплицируем и по нему же
# находим/перезаписываем уже стоящую строку.
PUBKEY_CONTENT="$(cat "${PUB_KEY}")"

# Стоит ли наш ключ на той стороне УЖЕ с непустым префиксом key-options (т.е.
# ограниченный — from=/restrict/...)? Проверяем по ключу в BatchMode: ищем строку
# с телом ключа, где ПЕРЕД телом есть хоть какие-то options.
#   exit 0  => стоит ограниченным (апгрейд не нужен);
#   exit 1  => строки нет ИЛИ она ГОЛАЯ (без options) — нужна установка/апгрейд;
#   exit 2  => по ключу зайти не удалось (решение принимаем по другим сигналам).
#
# РЕАЛИЗАЦИЯ через awk с index() (ДОСЛОВНОЕ сравнение подстрок), а НЕ regex:
# тело ed25519-ключа в base64 содержит '+' и '/' — это метасимволы ERE, и
# grep -E по телу ключа ложно бы (не) срабатывал. awk index() сравнивает буквально.
# Логика на удалённой стороне (BAREKEY = всё тело "ssh-ed25519 AAAA... comment",
# KEYTYPE = первое поле тела, напр. "ssh-ed25519"):
#   строка СОДЕРЖИТ ключ  := index($0, BAREKEY) > 0;
#   строка ГОЛАЯ          := содержит ключ И её ПЕРВОЕ поле == KEYTYPE
#                            (т.е. тело идёт без префикса options);
#   строка ОГРАНИЧЕНА     := содержит ключ И первое поле != KEYTYPE (есть options).
key_already_restricted() {
    local out rc
    out="$(printf '%s\n' "${PUBKEY_CONTENT}" | \
        ssh "${SSH_COMMON_OPTS[@]}" \
            -o BatchMode=yes \
            -o IdentitiesOnly=yes \
            -i "${PRIV_KEY}" \
            "${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}" \
            'IFS= read -r BAREKEY; \
             [ -n "$BAREKEY" ] || { echo NOLOGIN; exit 0; }; \
             f=~/.ssh/authorized_keys; \
             [ -f "$f" ] || { echo MISSING; exit 0; }; \
             KEYTYPE=${BAREKEY%% *}; \
             awk -v key="$BAREKEY" -v kt="$KEYTYPE" '"'"' \
                 index($0, key) { found=1; if ($1==kt) bare=1; else restr=1 } \
                 END { if (restr) print "RESTRICTED"; \
                       else if (bare) print "BARE"; \
                       else print "MISSING" } \
             '"'"' "$f"' 2>/dev/null)" || true
    rc=2
    case "${out}" in
        RESTRICTED) rc=0 ;;
        BARE|MISSING|NOLOGIN) rc=1 ;;
        *) rc=2 ;;
    esac
    return "${rc}"
}

# --- Решаем, что делать: пропустить / установить / апгрейдить ---------------
# ВАЖНО (дедуп-регрессия): нельзя «закорачивать» на key_login_works(). Вход по
# ключу может работать ИМЕННО потому, что ранее ssh-copy-id поставил ГОЛУЮ строку
# (без options). Тогда повторный запуск ОБЯЗАН проапгрейдить её до ограниченной,
# а не радостно пропустить установку. Поэтому сначала спрашиваем не «пускает ли
# ключ», а «стоит ли он ОГРАНИЧЕННЫМ».
NEED_INSTALL=1   # 1 => нужно ставить/апгрейдить; 0 => уже ограниченный, пропускаем
if key_already_restricted; then
    NEED_INSTALL=0
    log "Ключ на ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP} УЖЕ стоит С ограничениями — установку пропускаю."
elif [[ $? -eq 1 ]] && key_login_works; then
    # Вход по ключу работает, но строка ГОЛАЯ (или ключа нет, а пускает другой) —
    # будем апгрейдить/доставлять. Это и есть фикс дедуп-регрессии.
    log "Вход по ключу работает, но строка БЕЗ ограничений (голая) — выполню АПГРЕЙД до ограниченной."
else
    log "Ограниченный ключ на той стороне не обнаружен — установлю его."
fi

if [[ "${NEED_INSTALL}" -eq 0 ]]; then
    # Уже ограниченный ключ. Но SERVER_EGRESS_IP/KEY_OPTIONS для сводки всё равно
    # хотим осмысленные — добываем источник-IP по ключу (без пароля), иначе HTTP.
    src_now="$(src_ip_via_key)"
    [[ -z "${src_now}" ]] && src_now="$(get_egress_ip_http)"
    build_key_options "${src_now}"
else
    log "Готовлю установку/апгрейд ключа на зарубежном VPS."
    warn "Если ключ ещё не пускает — потребуется ОДИН раз ввести пароль ${FOREIGN_SSH_USER}."
    # Установка/апгрейд может требовать интерактивного ввода пароля. Без управляющего
    # терминала (cron/CI/pipe) пароль ввести некуда — предупреждаем заранее.
    if [[ ! -t 0 || ! -t 1 ]]; then
        warn "Похоже, нет управляющего терминала (TTY). Интерактивный ввод пароля для"
        warn "установки ключа может не сработать. Запустите скрипт в интерактивной сессии"
        warn "или заранее разместите ключ в ~/.ssh/authorized_keys на зарубежном VPS."
    fi

    # --- Определяем источник-IP для from=, как его видит зарубежный VPS --------
    # Сначала пробуем по уже работающему ключу (без пароля). Если ключ ещё не
    # пускает, источник всё равно будет надёжно получен ВНУТРИ установочной
    # сессии ниже (там удалённая команда сама прочитает $SSH_CONNECTION и
    # подставит from= по фактическому client-IP). HTTP-проба — последний резерв.
    SRC_IP="$(src_ip_via_key)"
    if valid_ipv4 "${SRC_IP}"; then
        log "Источник-IP (виден зарубежному VPS по ключу): ${SRC_IP}."
    else
        SRC_IP=""   # получим внутри установочной сессии из $SSH_CONNECTION
    fi

    copied=0
    # ОСНОВНОЙ путь — ручная установка/апгрейд options-префиксированной строки.
    # ssh-copy-id НЕ умеет добавлять key-options, поэтому ставить ключ нужно вручную,
    # иначе ограничения (from=/restrict) просто не применятся. ssh-copy-id оставлен
    # ниже ТОЛЬКО как аварийный фолбэк (с громким предупреждением, что он ставит
    # ключ БЕЗ ограничений).
    #
    # Удалённый скрипт делает ВСЁ в рамках ОДНОЙ сессии (один пароль максимум):
    #   1) определяет реальный from-IP из $SSH_CONNECTION (первое поле) — это IP,
    #      с которого ключ РЕАЛЬНО пришёл; если SRC_IP передан с локальной стороны
    #      непустым, доверяем ему (он добыт тем же способом по ключу);
    #   2) собирает FULLLINE = "from=\"<ip>\",restrict,port-forwarding <key>"
    #      (или без from=, если IP не похож на IPv4);
    #   3) одним проходом awk: ГОЛУЮ строку (тело без options) перезаписывает на
    #      FULLLINE (АПГРЕЙД); строку с options оставляет как есть (идемпотентность);
    #      если строки нет вовсе — дописывает FULLLINE в конец.
    #
    # ПОЧЕМУ awk index(), а НЕ grep -E/sed: тело ed25519-ключа в base64 содержит
    # '+' и '/' — метасимволы регулярок, из-за которых grep -E/sed по телу ключа
    # давали бы ложные (не)срабатывания. awk index() сравнивает ДОСЛОВНО.
    # "Голая" строка := её ПЕРВОЕ поле == KEYTYPE (тип ключа, напр. ssh-ed25519),
    # т.е. тело идёт без префикса options.
    #
    # ВАЖНО про stdin: тело ключа (BAREKEY) и подсказку SRC_IP передаём в удалённую
    # команду через stdin двумя строками. Пароль OpenSSH запрашивает с управляющего
    # терминала (/dev/tty), а НЕ со stdin, поэтому одно не мешает другому — но только
    # при наличии TTY (см. предупреждение выше).
    #
    # IdentitiesOnly=yes (как и в key_login_works): чтобы случайный ключ из ssh-agent
    # молча НЕ сработал вместо пароля и не залогинил нас не тем ключом/пользователем.
    #
    # Удалённый скрипт (НИЖЕ) намеренно БЕЗ inline-#-комментариев: строка собирается
    # локально через '\'-переносы, а '\'+newline ВНУТРИ одинарных кавычек сохраняется
    # ДОСЛОВНО и на удалённой стороне работает как продолжение строки — '#'-комментарий
    # «съел» бы следующую команду.
    log "Устанавливаю/апгрейжу ключ с ограничениями key-options (from= из \$SSH_CONNECTION)..."
    if printf '%s\n%s\n' "${PUBKEY_CONTENT}" "${SRC_IP}" | \
        ssh "${SSH_COMMON_OPTS[@]}" \
            -o IdentitiesOnly=yes \
            -i "${PRIV_KEY}" \
            "${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}" \
            'IFS= read -r BAREKEY; IFS= read -r SRCHINT; \
             [ -n "$BAREKEY" ] || { echo "EMPTY_KEY" >&2; exit 3; }; \
             set -- $SSH_CONNECTION; SRCIP="$1"; \
             [ -n "$SRCIP" ] || SRCIP="$SRCHINT"; \
             case "$SRCIP" in \
               *[!0-9.]*|"" ) OPTS="restrict,port-forwarding" ;; \
               * ) OPTS="from=\"$SRCIP\",restrict,port-forwarding" ;; \
             esac; \
             FULLLINE="$OPTS $BAREKEY"; \
             KEYTYPE=${BAREKEY%% *}; \
             umask 077; \
             mkdir -p ~/.ssh; \
             touch ~/.ssh/authorized_keys; \
             f=~/.ssh/authorized_keys; \
             tmp="$(mktemp "$(dirname "$f")/ak.XXXXXX")" || { echo "MKTEMP_FAIL" >&2; exit 4; }; \
             awk -v key="$BAREKEY" -v kt="$KEYTYPE" -v full="$FULLLINE" '"'"' \
                 index($0, key) { seen=1; if ($1==kt) { print full; next } } \
                 { print } \
                 END { if (!seen) print full } \
             '"'"' "$f" > "$tmp" && cat "$tmp" > "$f"; \
             rm -f "$tmp"; \
             chmod 700 ~/.ssh; chmod 600 "$f"; \
             echo "SRCIP=$SRCIP"'; then
        copied=1
        log "Ключ установлен/проапгрейжен в ~/.ssh/authorized_keys на зарубежном VPS С ограничениями."
    else
        warn "Ручная установка/апгрейд с ограничениями не удалась — пробую фолбэк через ssh-copy-id."
    fi

    # После ручного пути уточняем фактический источник-IP по уже стоящему ключу
    # (теперь вход должен работать). Это и заполнит KEY_OPTIONS/SERVER_EGRESS_IP
    # реальным from=, который применился на той стороне.
    if [[ "${copied}" -eq 1 ]]; then
        src_after="$(src_ip_via_key)"
        [[ -z "${src_after}" ]] && src_after="${SRC_IP}"
        [[ -z "${src_after}" ]] && src_after="$(get_egress_ip_http)"
        build_key_options "${src_after}"
    fi

    if [[ "${copied}" -ne 1 ]]; then
        if command -v ssh-copy-id >/dev/null 2>&1; then
            # ФОЛБЭК: ssh-copy-id дедуплицирует и чинит права, НО ставит ключ БЕЗ
            # key-options — ограничения from=/restrict/port-forwarding НЕ применятся.
            # Здесь $SSH_CONNECTION нам недоступен, поэтому источник-IP для подсказки
            # берём HTTP-пробой (последний резерв) и собираем KEY_OPTIONS для текста.
            build_key_options "$(get_egress_ip_http)"
            log "Фолбэк: использую ssh-copy-id (БЕЗ ограничений)..."
            if ssh-copy-id \
                -i "${PUB_KEY}" \
                -p "${FOREIGN_SSH_PORT}" \
                -o ConnectTimeout=10 \
                -o StrictHostKeyChecking=accept-new \
                "${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}"; then
                copied=2   # 2 = установлен, но БЕЗ ограничений
                warn "================================================================"
                warn "ВНИМАНИЕ: ключ поставлен через ssh-copy-id БЕЗ key-options!"
                warn "Ограничения '${KEY_OPTIONS}' НЕ применены — ключ сейчас даёт"
                warn "неограниченный доступ с ЛЮБОГО IP. ВРУЧНУЮ допишите префикс"
                warn "  ${KEY_OPTIONS}"
                warn "к строке ИМЕННО ЭТОГО ключа в ~/.ssh/authorized_keys на зарубежном VPS."
                warn "Повторный запуск этого скрипта тоже сам проапгрейдит голую строку."
                warn "Ручной вариант (sed по ТЕЛУ КОНКРЕТНОГО ключа, не по всем ssh-ed25519):"
                warn "  ssh -p ${FOREIGN_SSH_PORT} ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP} \\"
                warn "    \"sed -i -E 's#^[[:space:]]*${PUBKEY_CONTENT}([[:space:]].*)?\$#${KEY_OPTIONS} ${PUBKEY_CONTENT}#' ~/.ssh/authorized_keys\""
                warn "================================================================"
            else
                warn "ssh-copy-id тоже завершился с ошибкой."
            fi
        else
            warn "ssh-copy-id недоступен — фолбэк невозможен."
        fi
    fi

    if [[ "${copied}" -eq 0 ]]; then
        err "Не удалось скопировать публичный ключ на зарубежный VPS ни одним способом."
        exit 1
    fi
fi

# ============================================================================
#  5. ПРОВЕРКА ВХОДА ПО КЛЮЧУ (BatchMode — без интерактива)
# ============================================================================

log "Проверяю беспарольный вход по ключу (BatchMode)..."
if key_login_works; then
    log "OK: вход по ключу на ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}:${FOREIGN_SSH_PORT} работает без пароля."
else
    err "Беспарольный вход по ключу НЕ удался. Проверьте:"
    err "  - доступность ${FOREIGN_VPS_IP}:${FOREIGN_SSH_PORT} с этого сервера;"
    err "  - что на зарубежном VPS sshd разрешает PubkeyAuthentication yes;"
    err "  - для root: PermitRootLogin prohibit-password (или yes) в sshd_config;"
    err "  - права: ~/.ssh = 700, ~/.ssh/authorized_keys = 600 на зарубежной стороне;"
    err "  - диагностика: ssh -v -i ${PRIV_KEY} -p ${FOREIGN_SSH_PORT} ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}"
    exit 1
fi

# ============================================================================
#  6. FINGERPRINT КЛЮЧА (для сверки/аудита)
# ============================================================================
# Источник-IP (для from="...") уже определён на шаге 4 в рамках установочной
# сессии (из $SSH_CONNECTION) или фолбэком, поэтому здесь его повторно НЕ считаем.

# Подстраховка для сводки: KEY_OPTIONS/SERVER_EGRESS_IP должны быть заполнены к
# этому месту (build_key_options вызывается на всех путях шага 4). Но если по
# какой-то причине пусто — подставим безопасные значения для текста.
[[ -n "${KEY_OPTIONS}" ]] || KEY_OPTIONS="restrict,port-forwarding"
[[ -n "${SERVER_EGRESS_IP}" ]] || SERVER_EGRESS_IP="неизвестен (определите вручную: curl https://api.ipify.org)"

# Fingerprint публичного ключа — для сверки/аудита.
KEY_FP="$(ssh-keygen -lf "${PUB_KEY}" 2>/dev/null || echo 'неизвестен')"

# ============================================================================
#  7. ИТОГОВАЯ СВОДКА
# ============================================================================
cat <<EOF

============================================================
  SSH-КЛЮЧ РФ-СЕРВЕР -> ЗАРУБЕЖНЫЙ VPS: ГОТОВ
============================================================
  Приватный ключ:  ${PRIV_KEY}      (chmod 600)
  Публичный ключ:  ${PUB_KEY}       (chmod 644)
  Fingerprint:     ${KEY_FP}

  Доступ настроен на:
    ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}:${FOREIGN_SSH_PORT}  (беспарольно, по ключу)

  Ключ ОГРАНИЧЕН в authorized_keys (key-options):
    ${KEY_OPTIONS}
      restrict        — отключены shell/pty/agent/X11;
      port-forwarding — оставлен только форвардинг (нужен для ssh -D/-L и autossh);
      from="..."      — ключ принимается только с того IP, который зарубежный
                        VPS реально видит у этого РФ-сервера (взят из
                        \$SSH_CONNECTION при установке — точнее HTTP-пробы на
                        multi-homed/policy-routed хосте; если from= нет в строке
                        выше — IP определить не удалось, см. предупреждения и
                        допишите from= вручную).

  Источник-IP этого РФ-сервера, как его видит зарубежный VPS (для бастиона/whitelisting):
    ${SERVER_EGRESS_IP}

  Дальнейшие шаги:
    - Бастион / ProxyJump:
        ssh -J ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP}:${FOREIGN_SSH_PORT} <конечный-хост>
    - Туннель autossh (пример прямого SOCKS/порт-форварда):
        autossh -M 0 -i ${PRIV_KEY} -p ${FOREIGN_SSH_PORT} \\
                ${FOREIGN_SSH_USER}@${FOREIGN_VPS_IP} ...

  Безопасность (дополнительно на зарубежной стороне):
    - ограничение по ключу УЖЕ применено (from=/restrict/port-forwarding выше);
    - используйте отдельного непривилегированного пользователя вместо root;
    - закройте SSH-порт фаерволом всем, кроме источника-IP РФ-сервера.
============================================================
EOF

log "Готово."
